                                <p>The hazards of using traditional materials in construction are well-documented. Not the least of these is the presence of volatile organic compounds (VOCs), which have a proven association with increased cancer risks and other health problems.</p>
                                <p>Green products are free of such contaminants. For example, wheat straw can be formed into sheets that perform the same function as plywood, without the use of formaldehyde. Vinyl-free floor coverings don&rsquo;t contain that substance&rsquo;s many toxins.</p>
                                <p>Being guided by such facts in selecting construction materials offers many benefits. It enhances the overall environment in the completed structure, leading (in the case of commercial buildings) to more productive employees, and (in the case of residential dwellings) to healthier, more physically active occupants.</p>
