                    <p><strong>Grey water:</strong> Any water used in households, except water from toilets, is called grey water. This includes washings from shower, sink, kitchen sinks, and laundry water. This can be reused for various applications, especially landscape irrigation.</p>
                    <p><strong>Black water:</strong> Refers to sewage water from toilets.</p>
                    <ul>
                        <li>Sewage treatment plants based on biological processes are commonly used for treatment of waste water that includes both grey water and black water. These are dependent on natural microorganisms, which utilize oxygen and the organic contaminants in the waste water to generate CO<sub>2</sub>, sludge, and treated water.</li>
                        <li>In the treatment systems, microorganisms exist in suspended form (for example, in an aeration tank, microbes are present freely in waste water without any support) or attached form (such as reed bed systems, where microbes are attached to the roots of plants, sand, and gravel).</li>
                        <li>These systems normally require a pre-treatment step such as a settlement chamber before the aeration unit.</li>
                        <li>Artificial wetlands or reed bed systems, which are based on natural processes, are beneficial due to simple and low O&amp;M cost.</li>
