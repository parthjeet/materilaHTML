                        <li><strong>Glare:</strong> <br> <br>

                        <ul>
                            <li>Glare is usually caused by excessive luminance differences in the visual field.</li>
                            <li>In indoor lighting the main concern is about discomfort glare. This is visual discomfort
                                in the presence of bright light sources, luminaries, windows or other bright surfaces
                                (Boyce 2003).
                            </li>
                            <li>There are established systems for the evaluation of the magnitude of discomfort glare,
                                e.g. <strong>Unified Glare Rating (UGR) (EN 12464-1 2002), Visual Comfort Probability
                                    (VCP) (IESNA 2000).</strong></li>
                            <li>The <strong>CIE Unified Glare Rating (UGR) scale</strong> is an interval scale where the
                                differences between numbers represent the perceptible differences in psychological
                                value. Only differences are meaningful, the value assigned to the lowest value is
                                arbitrary. It is limited to direct sources within the 0.0003~0.1 steradians range. One
                                glare rating unit is the least detectable step and three glare rating units is an
                                acceptable step in terms of glare criteria. The practical range is 10-30 with most
                                systems producing values in that range. It is sufficient to characterize systems with
                                UGR below 10 as simply UGR&lt;10 (Huang 2008).
                            </li>
                            <li>The present glare indices are best suitable for assessing discomfort glare induced by a
                                regular array of fluorescent lamp luminaries for a range of standard interiors.
                            </li>
                            <li>The possible problems are related to the definition of the glare source size and
                                luminance and its immediate background luminance (Boyce 2003).
                            </li>
                            <li>It is also necessary to achieve sufficiently bright interior surfaces to avoid glare
                                from contrast with the sky (Casey 2003).
                            </li>
                            <li>For daylight inclusion, Glare Index, a value for predicting the presence of glare as a
                                result of daylight entering an area, is used (Green Park 2004).
                            </li>
                            <li>The glare index is affected by the size and relative position of fenestration,
                                orientation to the sun, sky luminance, and interior luminances. The glare index is
                                similar to the index of sensation and the above discomfort glare rating, which are used
                                for electric lighting applications. <br> <br>
                            </li>
                        </ul>
