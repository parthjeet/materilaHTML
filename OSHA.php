                <p><strong>Occupational noise exposure</strong><strong>- </strong></p>
                <p><strong>Hearing protectors- </strong>The employer shall provide training in the use and care of all hearing protectors provided to employees</p>
                <p><strong>Training program- </strong>The employer shall institute a training program for all employees who are exposed to noise at or above an 8-hour time weighted</p>
                <p>average of 85 decibels, and shall ensure employee participation in such program.</p>
                <p>The training program shall be repeated annually for each employee included in the hearing conservation program. Information provided in the training program shall be updated to be consistent with changes in protective equipment and work processes.</p>
                <p>The employer shall ensure that each employee is informed of the&nbsp;following:</p>
                <ol>
                    <li>The effects of noise on hearing;</li>
                    <li>The purpose of hearing protectors, the advantages, disadvantages, and attenuation of various types, and instructions on selection, fitting, use, and care; and</li>
                    <li>The purpose of audiometric testing, and an explanation of the test procedures.</li>
                </ol>
