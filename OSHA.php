                <p>With the Occupational Safety and Health Act of 1970, Congress created the Occupational Safety and Health Administration (OSHA) to assure safe and healthful working conditions for working men and women by setting and enforcing standards and by providing training, outreach, education and assistance.</p>
                <p>OSHA is part of the United States Department of Labor. The administrator for OSHA is the Assistant Secretary of Labor for Occupational Safety and Health. OSHA's administrator answers to the Secretary of Labor, who is a member of the cabinet of the President of the United States.</p>
                <p><strong>Training Requirements in OSHA Standards</strong></p>
                <p><strong>Emergency action plans-</strong> An employer must have an emergency action plan whenever an OSHA standard in this part requires one. The requirements in this section apply to each such emergency action plan</p>
                <p>An emergency action plan must include at a minimum:</p>
                <ol>
                    <li>Procedures for reporting a fire or other emergency</li>
                    <li>Procedures for emergency evacuation, including type of evacuation and exit route assignments</li>
                    <li>Procedures to be followed by employees who remain to operate critical plant operations before they evacuate;</li>
                    <li>Procedures to account for all employees after evacuation;</li>
                    <li>Procedures to be followed by employees performing rescue or medical duties</li>
                    <li>The name or job title of every employee who may be contacted by employees who need more information about the plan or an explanation of their duties under the plan.</li>
                </ol>
                <p><strong>Employee alarm system.</strong> An employer must have and maintain an employee alarm system. The employee alarm system must use a distinctive signal for each purpose and comply with the requirements</p>
                <p><strong>Training.</strong> An employer must designate and train employees to assist in a safe and orderly evacuation of other employees.</p>
                <p><strong>Review of emergency action plan.</strong> An employer must review the emergency action plan with each employee covered by the plan:</p>
                <ol>
                    <li>When the plan is developed or the employee is assigned initially to a job</li>
                    <li>When the employee&rsquo;s responsibilities under the plan change</li>
                    <li>When the plan is changed</li>
                </ol>
