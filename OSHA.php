                <p><strong>Fire prevention plans</strong><strong>- </strong>An employer must have a fire prevention plan when an OSHA standard in this part requires one. The requirements in this section apply to each such fire prevention plan.</p>
                <p><strong>Written and oral fire prevention plans. </strong>A fire prevention plan must be in writing, be kept in the workplace, and be made available to employees for review. However, an employer with 10 or fewer employees may communicate the plan orally to employees.</p>
                <p><strong>Minimum elements of a fire prevention plan.</strong> A fire prevention plan must include:</p>
                <ol>
                    <li>A list of all major fire hazards, proper handling and storage procedures for hazardous materials, potential ignition sources and their control, and the type of fire protection equipment necessary to control each major hazard</li>
                    <li>Procedures to control accumulations of flammable and combustible waste materials</li>
                    <li>Procedures for regular maintenance of safeguards installed on heat-producing equipment to prevent the accidental ignition of combustible materials;</li>
                    <li>The name or job title of employees responsible for maintaining equipment to prevent or control sources of ignition or fires; and</li>
                    <li>The name or job title of employees responsible for the control of fuel source hazards.</li>
                </ol>
                <p><strong>Employee information. </strong>An employer must inform employees upon initial assignment to a job of the fire hazards to which they are exposed. An employer must also review with each employee those parts of the fire prevention plan necessary for self-protection.</p>
