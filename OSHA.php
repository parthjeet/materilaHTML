                    <li>When PPE is necessary;</li>
                    <li>What PPE is necessary;</li>
                    <li>How to properly don, doff, adjust and wear PPE;</li>
                    <li>The limitations of the PPE; and,</li>
                    <li>The proper care, maintenance, useful life, and disposal of the PPE.</li>
                </ol>
                <p><strong>Respiratory protection</strong></p>
                <p>Training and information. This paragraph requires the employer to provide effective training to employees who are required to use respirators. The training must be comprehensive, understandable, and recur annually, and more often if necessary. This paragraph also requires the employer to provide the basic information on respirators to employees who wear respirators when not required by this section or by the employer to do so. The employer shall ensure that each employee can demonstrate knowledge of at least the following:</p>
                <ol>
                    <li>Why the respirator is necessary and how improper fit, usage, or maintenance can compromise the protective effect of the respirator;</li>
                    <li>What the limitations and capabilities of the respirator are;</li>
                    <li>How to use the respirator effectively in emergency situations, including situations in which the respirator malfunctions;</li>
                    <li>How to inspect, put on and remove, use, and check the seals of the respirator;</li>
                    <li>What the procedures are for maintenance and storage of the respirator;</li>
                    <li>How to recognize medical signs and symptoms that may limit or prevent the effective use of respirators; and</li>
                </ol>
                <p>The general requirements of this section.</p>
