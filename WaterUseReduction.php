                    <p>Grey water from bathrooms, kitchens, and other washings can be suitably treated and reused for non-potable applications such as irrigation and flushing. Separation of the grey water from black water by installation of dual plumbing lines at the time of construction is economical. Rainwater can be harvested from rooftops, paved and unpaved areas, storm water drains, and water bodies.</p>
                    <p>The basic concept of storing and recharging of water</p>
                    <ul>
                        <li>The recharging or storing of water depends on the rainfall of a particular region, and the sub-surface geology.</li>
                        <li>In regions where the rainy season lasts for three to four months, groundwater recharge is beneficial rather than storage, as the storage cistern would remain empty during other parts of the year.</li>
                        <li>In places where the surface is impermeable and groundwater is saline or not of potable quality, it is not advisable to go for groundwater recharging.</li>
                        <li>Recharging can be done through dugwells, borewells, recharge trenches, and recharge pits. Filter material at the entry point is essential to maintain the quality of water.</li>
                        <li>A settlement tank acts as a buffer to hold the surplus water during the course of excess rainfall.</li>
                    </ul>
                    <p><strong>Infiltration techniques</strong></p>
                    <ul>
                        <li>Trenches filled with rocks receive storm water runoff from grass swales and water percolates through the void space of rocks into soil.</li>
                        <li>Bio filtration swale/grass swale is a vegetated channel with slope less than 0.6%, so as to increase the flow residence time, increase the pollutant (suspended solids and trace metals) removal efficiency by effective infiltration, and reduce run-off.</li>
                    </ul>
                    <p><strong>Sand filter</strong></p>
                    <p>Use of sand layer for filtering the storm water into drainage facility before storage in detention basins. This results in removal of total suspended solids. The efficiency is higher if the pre-treatment is achieved through trench and grass swales.</p>
                    <p><strong>Detention basins</strong></p>
                    <ul>
                        <li><em>Wet ponds:</em> constructed ponds to retain the filtered storm water.</li>
                        <li><em>Storm water wetlands:</em> wet ponds integrated with wetland plants, which will also facilitate storm water treatment.</li>
                        <li><em>Wet vaults and storage tanks:</em> underground storage facilities in reinforced cement concrete used for irrigation at a later stage.</li>
                    </ul>
