            <p class="h4"><strong>Seven principles of Xeriscape</strong></p>
            <div class="table-responsive">
                <table class="table table-bordered table-striped-green text-justify">
                <tbody>
                <tr>
                    <td>
                        <p>1. Proper and efficient landscape planning and design that utilizes natural resources to the maximum and minimizes the environmental impacts of development on the ecosystem.</p>
                        <p>2. Se the restoration of native vegetation.</p>
                        <p>3. Analysis of soil and climate to assess the potential of natural resources, such as rainwater sunlight, and wind; protection the top soil cover from chemical wastes, or control of erosion and sedimentation.</p>
                        <p>4. Reduction of high maintenance turf area.</p>
                        <p>5. Use of mulching or composting to aid plant growth, and retain soil fertility and moisture.</p>
                        <p>6. Appropriate maintenance through a comprehensive monitoring system.</p>
                        <p>7. Efficient irrigation system that groups together plans with similar waste and maintenance needs. Link irrigation system with natural water-harvesting systems.</p>
