                    <td>
                        <p>&sect; Analyze the climate zone into which the site falls (hot-dry, hot-humid, cold &amp; temperate)</p>
                        <p>&sect; Investigate the climatically responsive historical architecture &amp; building design practices amenable to that zone</p>
