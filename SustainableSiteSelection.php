                    </td>
                    <td>
                        <p>&sect; The design team should measure the scale &amp; amount of energy, labor and material inputs available on site- in the form of natural resources or feature-against the cost input in the site development cycle.</p>
                        <p>&sect; The team should also see whether the waste output can be dealt with acceptable environmental costs, and whether development impacts can be minimized on-site.</p>
