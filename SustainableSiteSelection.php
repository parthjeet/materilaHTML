                        <p><em>Assess the existing air quality of the site environment</em></p>
                    </td>
                    <td>
                        <p>&sect; Determine at the pre-design stage whether it is free of noxious chemicals and pollutants, hazardous to development</p>
                        <p>&sect; Ensure that the development will not further deteriorate the air quality</p>
