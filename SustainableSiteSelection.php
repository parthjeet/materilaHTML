            <p class="h4"><strong>RECOMMENDATIONS AND PRACTICES FOR UTILITY/INFRASTRUCTURE</strong></p>
            <div class="table-responsive">
                <table class="table table-bordered table-striped-green text-justify">
                <tbody>
                <tr>
                    <td>
                        <p>1. Integrate the existing utility and transportation infrastructure and capacity into the proposed development in order to minimize the need for new infrastructure.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>2. Select utility systems for water and sanitation, gas, heating or cooling, and storm drainage that are in harmony with the sustainable design principles and with the functioning of the established natural ecosystem.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>3. Use gravity sewer systems- as against the pumped sewer systems- to reduce power consumption and develop a sustainable natural system instead.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>4. Plan utility corridors (for utility lines such as sewage, power and network transmission, water, gas, and storm water) near other corridor areas that have already been disturbed, such as roads and pedestrian paths.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>5. Site utility systems with the landscape.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>6. Analyze the existing road system and network for parking and for pedestrians to form site circulation patterns.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>7. Design the site plan in a manner so as to minimize the length of primary or secondary circulation, pedestrian walkways, utility lines, and building footprint.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>8. Improve site safety and security.</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>9. Plan for alternative traffic/transportation strategies that encourage the sharing of vehicles, parking areas, and transportation costs (such as car pools mass-transit infrastructures, etc.)</p>
                    </td>
                </tr>
                <tr>
                    <td>
                        <p>10. Consider and incorporate the popular use of telecommuting strategies, such as tele-conferencing or net-conferencing, which reduces the commuting time and the frequency of travel to the site.</p>
                    </td>
                </tr>
                </tbody>
