                    </td>
                    <td>
                        <p>&sect; Retain existing native vegetation as much as possible to secure the integrity of the site.</p>
                        <p>&sect; Reduce fragmentation &amp; canopy loss in the landscape to support the survival ability of the various plant communities &amp; habitats.</p>
                        <p>&sect; Develop an interconnect &amp; interdependent landscape</p>
