                   <h3>2. FINISHING DEPARTMENT</h3>
                    <p>Generally, this process includes checking of the garment, measurement checking, ironing, and spotting. After sewing of the garments, all pieces are checked by the quality checker to ensure that garments have been made as per buyer quality standards. Checking is normally done for visual appearance and measurements. Spotting is required to remove stains in the pieces. Special chemicals, (solvents) are used to remove various kinds of oil stains, marks and hard stains. Each garment is then ironed with a press to remove creases. </p>
                    <p>Each pressed garment is now folded with tissue or cardboard. Folding varies from product to product and also from buyer to buyer. Hang tags, special tags and price stickers are attached with plastic Kimble or threads. Folded and tagged garments are then packed into poly bags. During packing, garments are randomly checked by internal quality controllers to ensure that only quality goods are being packed.</p>
