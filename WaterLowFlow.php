                    <ul>
                        <li><strong>Water efficient fixtures</strong>

                            <p>Use of efficient plumbing fixtures, sensors, auto control valves, and pressure reducing
                                devices wherever possible can result in significant reduction in water consumption.</p>
                            <ul>
                                <li>Conventional toilets use 13.5 liters of water per flush. Low flush toilets are
                                    available with a flow rate of 6.0 liters and 3.0 liters of water per flush.
                                </li>
                                <li>Dual flush adapters can be used for standard flushing for solid waste and a modified
                                    smaller flush for liquid waste.
                                </li>
                                <li>Flush valves with 20&ndash;25 mm inlets can be used for restricting the water
                                    flow.
                                </li>
                                <li>Composting toilets.</li>
                                <li>Water-efficient urinals.</li>
                                <li>Conventional urinals use water at a rate of 7.5&ndash;11 liters per flush. Use of
                                    electronic flushing system or magic eye sensor can further reduce the flow of water
                                    to 0.4 liters per flush.
                                </li>
                            </ul>


                            <p>Furthermore, waterless urinals do not use water.</p>
                        </li>

                        <li><strong>Auto control valves</strong>

                            <p>Installation of magic eye solenoid valves (self-operating valves) can result in saving
                                water.
                                Sensor taps have automatic on and off flow controls. These valves are not only
                                convenient
                                and hygienic but are also excellent water saving devices that can work under normal
                                water
                                pressure. They function with parameters such as distance and timing.</p>
                        </li>

                        <li><strong>Pressure reducing device</strong>

                            <p>Aerators and pressure inhibitors for constant flow. Use of aerators can result in flow
                                rates
                                as
                                low as 2 liters per minute, which is adequate for washing hands. Flow regulators are
                                installed
                                when aerators cannot be installed.</p>
                        </li>

                        <li><strong>Composting toilets</strong>

                            <p>Another means of reducing water for flushing toilets is to use composting toilets.
                                Composting
                                toilets are based on the biological process of converting solids present in human waste
                                into
                                enriched manure. A composting toilet consists of two underground pits. The first pit
                                will be
                                filled with waste over one to two years. During this time, bacteria digest the sludge.
                                After
                                two
                                years of digestion, the contents are odorless and safe to handle, and can be used as
                                soil
                                fertilizer. The second pit is used when the first is full.</p>
                        </li>
                    </ul>

