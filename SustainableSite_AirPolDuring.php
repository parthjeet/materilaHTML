                                <p>o Equip pulverizes with dust traps. For powders &gt;5mm, use separators and filter the dust from the exhaust air. For powders &lt;5mm, encapsulate the installation, trap and dispose the dust. Sometimes the material type, particle size or envisaged subsequent processing may not permit moistening. In such situations, or if the emission suppression is inadequate, then other countermeasures can be employed, which enable equivalent emission abatement.</p>
