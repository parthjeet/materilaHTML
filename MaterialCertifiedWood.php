                    <p><strong>Responsible forest management</strong> Includes:</p>
                    <ul>
                        <li>Protecting fragile ecosystems</li>
                        <li>Respecting native cultures and economies</li>
                        <li>Preventing illegal logging</li>
                        <li>Restricting clear-cutting (removing all trees in a tract) and pesticide use</li>
                        <li>Monitoring the &ldquo;chain of custody,&rdquo; or ensuring that the wood in the product you&rsquo;re looking at really came from the forest that was certified.</li>
                    </ul>
