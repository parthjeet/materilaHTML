                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>1</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>5S</p>
                            </td>
                            <td>
                                <p>5S is a five step methodology aimed at creating and maintaining an organized visual workplace.</p>
                                <p>&middot; Sort (eliminate that which is not needed)</p>
                                <p>&middot; Set In Order (organize remaining items)</p>
                                <p>&middot; Shine (clean and inspect work area)</p>
                                <p>&middot; Standardize (write standards for above)</p>
                                <p>&middot; Sustain (regularly apply the standards)</p>
                            </td>
                            <td>
                                <p>Eliminates waste that results from a poorly organized work area (e.g. wasting time looking for a tool).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>2</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Andon</p>
                            </td>
                            <td>
                                <p>Visual feedback system for the plant floor that indicates production status, alerts when assistance is needed, and empowers operators to stop the production process.</p>
                            </td>
                            <td>
                                <p>Acts as a real-time communication tool for the plant floor that brings immediate attention to problems as they occur &ndash; so they can be instantly addressed.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>3</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Bottleneck Analysis</p>
                            </td>
                            <td>
                                <p>Identify which part of the manufacturing process limits the overall throughput and improve the performance of that part of the process.</p>
                            </td>
                            <td>
                                <p>Improves throughput by strengthening the weakest link in the manufacturing process.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>4</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Continuous Flow</p>
                            </td>
                            <td>
                                <p>This concept emphasises reducing the batch size in order to eliminate system constraints. A methodology by which a product or information is produced by moving at a consistent pace from one value-added processing step to the next with no delays in between.</p>
                            </td>
                            <td>
                                <p>Eliminates many forms of waste (e.g. inventory, waiting time, and transport).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>5</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Heijunka (Level Scheduling)</p>
                            </td>
                            <td>
                                <p>A form of production scheduling that purposely manufactures in much smaller batches by sequencing (mixing) product variants within the same process.</p>
                            </td>
                            <td>
                                <p>Reduces lead times (since each product or variant is manufactured more frequently) and inventory (since batches are smaller).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>6</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Hoshin Kanri (Policy Deployment)</p>
                            </td>
                            <td>
                                <p>Align the goals of the company (Strategy), with the plans of middle management (Tactics) and the work performed on the plant floor (Action).</p>
                            </td>
                            <td>
                                <p>Ensures that progress towards strategic goals is consistent and thorough &ndash; eliminating the waste that comes from poor communication and inconsistent direction.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>7</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Jidoka (Autonomation)</p>
                            </td>
                            <td>
                                <p>Design equipment to partially automate the manufacturing process (partial automation is typically much less expensive than full automation) and to automatically stop when defects are detected.</p>
                            </td>
                            <td>
                                <p>After Jidoka, workers can frequently monitor multiple stations (reducing labor costs) and many quality issues can be detected immediately (improving quality).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>8</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Just-In-Time (JIT)</p>
                            </td>
                            <td>
                                <p>Pull parts through production based on customer demand instead of pushing parts through production based on projected demand. Relies on many lean tools, such as&nbsp;Continuous Flow,&nbsp;Heijunka,&nbsp;Kanban,&nbsp;Standardized Work&nbsp;and&nbsp;Takt Time.</p>
                            </td>
                            <td>
                                <p>Highly effective in reducing inventory levels. Improves cash flow and reduces space requirements.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>9</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Kaizen (Continuous Improvement)</p>
                            </td>
                            <td>
                                <p>A strategy where employees work together proactively to achieve regular, incremental improvements in the manufacturing process.</p>
                            </td>
                            <td>
                                <p>Combines the collective talents of a company to create an engine for continually eliminating waste from manufacturing processes.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>10</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Kanban (Pull System)</p>
                            </td>
                            <td>
                                <p>A method of regulating the flow of goods both within the factory and with outside suppliers and customers. Based on automatic replenishment through signal cards that indicate when more goods are needed.</p>
                            </td>
                            <td>
                                <p>Eliminates waste from inventory and overproduction. Can eliminate the need for physical inventories (instead relying on signal cards to indicate when more goods need to be ordered).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>11</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>KPIs (Key Performance Indicators)</p>
                            </td>
                            <td>
                                <p>Metrics designed to track and encourage progress towards critical goals of the organization. Strongly promoted KPIs can be extremely powerful drivers of behavior &ndash; so it is important to carefully select KPIs that will drive desired behavior.</p>
                            </td>
                            <td>
                                <p>The best manufacturing KPIs:</p>
                                <p>Are aligned with top-level strategic goals (thus helping to achieve those goals)</p>
                                <p>Are effective at exposing and quantifying waste (OEE is a good example)</p>
                                <p>Are readily influenced by plant floor employees (so they can drive results)</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>12.</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Overall Equipment Effectiveness (OEE)</p>
                            </td>
                            <td>
                                <p>Framework for measuring productivity loss for a given manufacturing process. Three categories of loss are tracked:</p>
                                <p>Availability (e.g. down time)</p>
                                <p>Performance (e.g. slow cycles)</p>
                                <p>Quality (e.g. rejects)</p>
                            </td>
                            <td>
                                <p>Provides a benchmark/baseline and a means to track progress in eliminating waste from a manufacturing process. 100% OEE means perfect production (manufacturing only good parts, as fast as possible, with no down time).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>13</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>PDCA (Plan, Do, Check, Act)</p>
                            </td>
                            <td>
                                <p>An iterative methodology for implementing improvements:</p>
                                <p>Plan (establish plan and expected results)</p>
                                <p>Do (implement plan)</p>
                                <p>Check (verify expected results achieved)</p>
                                <p>Act (review and assess; do it again)</p>
                            </td>
                            <td>
                                <p>Applies a scientific approach to making improvements:</p>
                                <p>Plan (develop a hypothesis)</p>
                                <p>Do (run experiment)</p>
                                <p>Check (evaluate results)</p>
                                <p>Act (refine your experiment; try again)</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>14</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Poka-Yoke (Error Proofing)</p>
                            </td>
                            <td>
                                <p>Design error detection and prevention into production processes with the goal of achieving zero defects.</p>
                                <p>A methodology that prevents an operator from making an error by incorporating preventive in-built responsiveness within the design of product or production process.</p>
                            </td>
                            <td>
                                <p>It is difficult (and expensive) to find all defects through inspection, and correcting defects typically gets significantly more expensive at each stage of production.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>15</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Root Cause Analysis</p>
                            </td>
                            <td>
                                <p>A problem solving methodology that focuses on resolving the underlying problem instead of applying quick fixes that only treat immediate symptoms of the problem. A common approach is to ask why five times &ndash; each time moving a step closer to discovering the true underlying problem.</p>
                            </td>
                            <td>
                                <p>Helps to ensure that a problem is truly eliminated by applying corrective action to the &ldquo;root cause&rdquo; of the problem.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>16</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Single-Minute Exchange of Dies (SMED)</p>
                            </td>
                            <td>
                                <p>Reduce setup (changeover) time to less than 10 minutes. Techniques include:</p>
                                <p>&middot; Convert setup steps to be external (performed while the process is running)</p>
                                <p>&middot; Simplify internal setup</p>
                                <p>&middot; Eliminate non-essential operations</p>
                                <p>&middot; Create&nbsp;Standardized Work&nbsp;instructions</p>
                            </td>
                            <td>
                                <p>Enables manufacturing in smaller lots, reduces inventory, and improves customer responsiveness.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>17</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Standardized Work</p>
                            </td>
                            <td>
                                <p>A process of documented description of methods, materials, tools, and processing times required to meet takt time for any given job. This aids in standardizing the tasks throughout the value stream.</p>
                            </td>
                            <td>
                                <p>Eliminates waste by consistently applying best practices. Forms a baseline for future improvement activities.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>18</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Takt Time</p>
                            </td>
                            <td>
                                <p>The pace of production that aligns production with customer demand. Calculated as Planned Production Time / Customer Demand.</p>
                                <p><strong>Takt time= </strong>Planned Production Time / Customer Demand.</p>
                            </td>
                            <td>
                                <p>Provides a simple, consistent and intuitive method of pacing production. Is easily extended to provide an efficiency goal for the plant floor (Actual Pieces / Target Pieces)</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>19</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Total Productive Maintenance (TPM)</p>
                            </td>
                            <td>
                                <p>A holistic approach to maintenance that focuses on proactive and preventative maintenance to maximize the operational time of equipment. TPM blurs the distinction between maintenance and production by placing a strong emphasis on empowering operators to help maintain their equipment.</p>
                            </td>
                            <td>
                                <p>Creates a shared responsibility for equipment that encourages greater involvement by plant floor workers. In the right environment this can be very effective in improving productivity (increasing up time, reducing cycle times, and eliminating defects).</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>20</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Value Stream Mapping</p>
                            </td>
                            <td>
                                <p>A tool used to visually map the flow of production. Shows the current and future state of processes in a way that highlights opportunities for improvement.</p>
                            </td>
                            <td>
                                <p>Exposes waste in the current processes and provides a roadmap for improvement through the future state.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>21</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Visual Factory</p>
                            </td>
                            <td>
                                <p>Visual indicators, displays and controls used throughout manufacturing plants to improve communication of information.</p>
                            </td>
                            <td>
                                <p>Makes the state and condition of manufacturing processes easily accessible and very clear &ndash; to everyone.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>22</p>
                            </td>
                            <td class="text-center align-middle">
                                <p>Cellular Manufacturing</p>
                            </td>
                            <td>
                                <p>Cellular manufacturing is an approach in which all equipment and workstations are arranged based on a group of different processes located in close proximity to manufacture a group of similar products.</p>
                            </td>
                            <td>
                                <p>Cellular manufacturing is to reduce cycle time and inventories to meet market response times.</p>
                            </td>
                        </tr>
                        </tbody>
