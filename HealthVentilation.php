                        <p class="h4"><strong>VENTILATION REQUIREMENTS</strong></p>
                        <p>Within a building, all enclosed spaces that are normally used by humans must be continuously ventilated during occupied hours with outdoor air, using either natural or mechanical ventilation. An exception is provided to refrigerated warehouses or other buildings or spaces that are not normally used for human occupancy or work.</p>
                        <p>The standards allow for ventilation to use transfer air as long as it doesn&rsquo;t have any &ldquo;unusual sources of indoor air contaminants&rdquo; and &ldquo;the outdoor air that is supplied to all spaces combined, is sufficient to meet the requirements. Good practice dictates that sources of contaminants be isolated and controlled with local exhaust. The designation and treatment of such spaces is subject to the designer&rsquo;s discretion.</p>
                        <p><strong>Natural Ventilation</strong></p>
                        <ul>
                            <li>Natural outdoor ventilation may be provided for spaces where all normally occupied areas of the space are within a specific distance from an operable wall or roof opening through which outdoor air can flow.</li>
                            <li>This distance is 20 ft. for most spaces and 25 ft. for hotel/motel guestrooms and high-rise residential spaces.</li>
                            <li>The sum of the operable open areas must total at least 5 percent of the floor area of each space that is naturally ventilated. The openings must also be readily accessible to the occupants of the space at all times.</li>
                            <li>Airflow through the openings must come directly from the outdoors; air may not flow through any intermediate spaces such as other occupied spaces, unconditioned spaces, corridors, or atriums. High windows or operable skylights need to have a control mechanism accessible from the floor.</li>
                        </ul>
                        <p><strong>Mechanical Ventilation</strong></p>
                        <ul>
                            <li>Mechanical outdoor ventilation must be provided for all spaces normally occupied that are not naturally ventilated.</li>
                            <li>The Standards require that a space conditioning system provide outdoor air equal to or exceeding the ventilation rates required for each of the spaces that it serves.</li>
                            <li>At the space, the required ventilation can be provided either directly through supply air or indirectly through transfer of air from the plenum or an adjacent space.</li>
                            <li>The required minimum ventilation airflow at the space can be provided by an equal quantity of supply or transfer air. At the air-handling unit, the minimum outside air must be the sum of the ventilation requirements of each of the spaces that it serves.</li>
                            <li>The designer may specify higher outside air ventilation rates based on the owner&rsquo;s preference or specific ventilation needs associated with the space.</li>
                            <li>However, specifying more ventilation air than the minimum allowable ventilation rates increases energy consumption and electrical peak demand and increases the costs of operating the HVAC equipment. Thus the designer should have a compelling reason to specify higher design minimum outside air rates than the calculated minimum outside air requirements in the Standards.</li>
                        </ul>
                        <p>(Source- <a href="https://energycodeace.com/site/custom/public/reference-ace-2013/index.html#!Documents/43ventilationrequirements.htm">Energy Code Ace</a>)</p>
                    <hr class="section-padding">
                    <p class="h4"><strong>WHY IS FRESH AIR REQUIRED IN THE WORKPLACE?</strong></p>
                        <p>You need to provide fresh air to:</p>
                        <ul>
                            <li>provide oxygen for breathing in and to remove carbon dioxide from breathing out;</li>
                            <li>remove excess heat or, if conditioned, provide heat e.g. in winter and keep a comfortable temperature;</li>
                            <li>dilute and remove body and other types of odors (e.g. food); and</li>
                            <li>dilute any contaminants caused by workplace activities (i.e. the use of 'dilution' ventilation following a risk assessment).</li>
                        </ul>
                    <hr class="section-padding">
                        <p class="h4"><strong>HOW TO ACHIEVE EFFECTIVE VENTILATION IN YOUR WORKPLACE</strong></p>
                        <ol>
                            <li>If your workplace is appropriately designed, for example by providing space for flexibility, and if the building is situated where there is enough shade to minimize summer heat, then you should be able to obtain adequate natural ventilation.</li>
                            <li>In most buildings make up air (air to replace that lost from the building) will be supplied indirectly through gaps around door? and window frames or directly through vents, windows, grilles and open doors.</li>
                            <li>But there are things you can do to make sure you have effective ventilation in your workplace:
                                <ul>
                                    <li>install trickle ventilators in window frames as well as having windows that open to provide controllable draught free ventilation (it is important that in winter any windows are well sealed and secure when closed)</li>
                                    <li>keep doors and windows open to provide a good supply of makeup air (in winter when the doors and windows are shut, you can supply make up air through air vents and extract any contaminated air using a suitable system e.g. via roof vents);</li>
                                    <li>position fans to extract contaminated air through an extraction point downwind or to supply fresh air on a wall and/or in the roof, in particular if your workplace has a large internal space (as in many industrial units, garages and warehouses);</li>
                                    <li>use fans on the wall of each side of the building to keep fresh air flowing throughout the building ie one extracting the contaminated air while the other supplies fresh air (make sure your employees work between the air supply and contaminant source).</li>
                                    <li>use a combination of extraction fans in the wall and/or roof, with open windows and doors to supply make up air;</li>
                                    <li>discharge any exhausted air above the roof and make sure that there is no opening for supply air near the exhaust discharge making allowance for wind effects;</li>
                                    <li>and locate any exhaust fans as near the source of the contamination as possible.</li>
                                </ul>
                            </li>
                            <li>Situate any extract and supply fan to make sure that there is enough air movement to remove stale and contaminated air from all parts of the room or building. Otherwise you may create poorly ventilated areas or 'dead zones'.</li>
                            <li>You can use portable fans to achieve an adequate air flow at specific sites within a building to keep employees cool or to remove contaminants generated from a particular process.</li>
                            <li>To keep internal conditions constant or to keep a process clean from airborne contaminants you can use a more complex mechanical system where the air is recirculated after being filtered.</li>
                        </ol>
