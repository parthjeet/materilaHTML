                    <p><strong>Use of renewable energy sources</strong></p>
                    <p>Renewable energy sources can provide the energy required for meeting the building energy demand. These sources are environmentally clean and no exhaustible. Natural sources of energy, such as solar, wind, hydropower, tidal energy, ocean thermal, and hydrogen, are all renewable energy sources. Projected availability of fossil fuels in future</p>
                    <p>and environmental degradation (including global warming) associated with usage of these fuels are the driving forces for increasing use of renewable energy.</p>
