                    <p><strong>PROGRAMME ON BIOMASS CO-GENERATION IN INDUSTRY</strong></p>
                    <p>The industrial sector consumes approximately 35% of total electricity generated in the country. Several industries require electrical as well as thermal energy for their operations, which can either be met through different energy sources or through co-generation using only one fuel. The power and steam generated from such co-generation plants can be used for meeting the captive requirements and the surplus power produced can be exported to the grid. Such projects are being set up in a number of industry sectors namely paper and pulp, solvent extraction, rice mills, pharmaceutical industries, etc.</p>
                    <p>The Ministry is implementing a Programme on &ldquo;Biomass Energy &amp; Cogeneration (non-bagasse) in industries&rdquo; for exploiting the vast potential of thermal energy and power for captive use in industry and captive electricity requirement of institutions, with the following major objectives:</p>
                    <ol>
                        <li>To encourage the deployment of biomass energy systems in industry for meeting thermal and electrical energy requirements.</li>
                        <li>To promote decentralized / distributed power generation through supply of surplus power to the grid.</li>
                        <li>To conserve fossil fuel use for captive requirements in industry.</li>
                    </ol>
                    <p>Capital subsidy on re-imbursement basis is being provided after commissioning of the projects @ 20 lakh per MW for biomass co-generation projects.</p>
