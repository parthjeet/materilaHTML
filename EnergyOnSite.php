                    <p><strong>Guidelines</strong></p>
                    <p>Guidelines for sizing, installation, and use of solar collectors</p>
                    <ul>
                        <li>Solar collectors should face south for maximum solar radiation collection.</li>
                        <li>The solar collector tilt should be equal to the latitude of the place for maximum annual energy collection.</li>
                        <li>The solar collector tilt should be equal to latitude + 150, gives maximum energy collection in winter.</li>
                        <li>The solar collector tilt should be equal to latitude -150, gives maximum energy collection in summer.</li>
                        <li>Always check the load carrying capacity of the roof before placing the solar collector. Typically, each solar collector of 2 m2 area weighs 50 kg.</li>
                        <li>The solar tank, when filled with water, weighs 1.2&ndash;1.4 kg per liter capacity of tank. (For example, 100-liter capacity tank weighs 120 kg)</li>
                        <li>Ensure proper anchoring of the system duly considering wind conditions.</li>
                        <li>Solar collectors and tank must be easily accessible for cleaning and maintenance.</li>
                        <li>A typical solar collector needs 1.3&ndash;1.5 times the collector area for installation. For example, a single collector system of 100-litre capacity, having 2 m2 area, requires an area of 3 m<sup>2 </sup>for installation.</li>
                    </ul>
                    <p><strong>Guidelines for solar collector selection and use</strong></p>
                    <ul>
                        <li>Check hardness of water to be used in solar system/collector? Solar collectors have small diameter pipes, which get chocked due to deposition of salt from hard water. In case of hard water, either water softener or heat-exchanger type solar water heater should be used.</li>
                        <li>It is a good practice to consider solar collector location and optimize the associated hot/cold water piping layout during the building design stage to reduce the cost and heat losses due to longer piping.</li>
                        <li>Always use good quality and long lasting pipes and insulation for trouble-free working.</li>
                        <li>It&rsquo;s important to check operating pressure of supply of cold water line,
                            especially when pressurized water is circulated. Most solar systems available in India are not designed for pressurized water supply.</li>

                        <li>Ensure continuous supply of water to the solar system for efficient and trouble-free operations.</li>
                        <li>When not in use, during long periods always cover the solar collectors with non-transparent covers (such as an old bed sheet or jute cloth)/to avoid overheating of solar system).</li>
                        <li>It is a good practice to use the entire hot water at a time.</li>
                        <li>Avoid using back up heater. Do not keep backup heater switched on.</li>
                        <li>Set the thermostat of back-up heater at55&ndash;60 &deg;C.</li>
                        <li>Use proper vent or vacuum release valve and pressure relief valve for safe operation of solar system.</li>
                        <li>Human body can tolerate temperature up to45 &deg;C. Human skin burns at water temperature above 55 &deg;C. Storage water heater temperature can be set at 55 + 5 &deg;C.</li>
                    </ul>
                    <p><strong>Guidelines for system sizing</strong></p>
                    <ul>
                        <li>Typically, a solar hot water system is sized to meet one day&rsquo;s requirement of hot water during winter. Typical hot water consumptions for various activities are given below. These can be used as guidelines for calculating total hot water requirement. (The consumption figures may vary depending on life style, age, habits, and weather conditions.)</li>
                        <li>For bathing using bucket water = 15 liter per person per day (one bucket).</li>
                        <li>For shower bath = 25 liter per person per day.</li>
                        <li>For bathtub = 35&ndash;50 liter per person per day.</li>
                        <li>For cooking = 5 liter per person per day.</li>
                        <li>For washing clothes = 10 liter per person per day.</li>
                        <li>For washing utensils and other purposes = 5 liter per person per meal.</li>
                        <li>For making tea/coffee = 150 ml per person per cup</li>
                    </ul>
