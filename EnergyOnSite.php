                    <p><strong><u>Types of Solar water heating system</u></strong></p>
                    <ol>
                        <li>Thermosiphon-type solar hot water system; <em>for capacities of upto 2000 liters per day</em></li>
                        <li>Forced flow solar hot water system; <em>for capacities higher than 2000 liters per day</em></li>
                    </ol>
                    <p><strong><u>Guidelines for Using Solar Water heating systems</u></strong></p>
                    <p><strong>Design, Installation and Use</strong></p>
                    <ul>
                        <li>Solar collector should face true south for maximum solar radiation collection</li>
                        <li>The solar collector tilt should be equal to the latitude of the place for maximum annual energy collection</li>
                        <li>The solar collector tilt equal to latitude +15̊ gives maximum energy collection in winter</li>
                        <li>The solar collector tilt equal to latitude -15̊ gives maximum energy collection in summer</li>
                        <li>Always check the load carrying capacity of the roof before placing the solar water heating system. Typically, each solar collector with a 2 m<sup>2 </sup>area weighs approximately 50 kg. When filled with water, the solar tank weighs about 1.2-1.4 kg per liter capacity of the tank (for example, a 100 liter capacity tank weighs around 120-130 kg)</li>
                        <li>Ensure proper anchoring of the system, duly considering wind conditions</li>
                        <li>Solar collectors and tank must be easily accessible for cleaning and maintenance. Typically, the solar water heating system requires approx. 1.3-1.5 times collector area for installation</li>
                    </ul>
                    <p><strong>System selection and use</strong></p>
                    <ul>
                        <li>Check the hardness of the water be used in the solar water heating system. Solar collectors have small diameter pipes, which get blocked due to the deposition of the salt from hard water. In cae of hard water, either a water heater can be use.</li>
                        <li>It is a good practice to consider the location of the solar water heating system and optimize the associated hot/cold water piping layout during the building design stage, to reduce cost and heat losses caused by longer piping.</li>
                        <li>Always use good quality pipes and insulation for longer use-life and trouble-free operation.</li>
                        <li>During long periods of no use, always cover the solar collectors with non-transparent covers to avoid the overheating,</li>
                        <li>It is a good practice to use all the hot water at one time</li>
                        <li>Avoid using a backup heater. Do not keep the backup heater switched on continuously.</li>
                        <li>Set the thermostat of the backup heater at 55-60̊ C</li>
                        <li>Use a proper vent or vacuum release valve/ pressure relief valve for safe operation of the solar water heating system.</li>
                    </ul>
                    <p><strong>Insulated hot water piping</strong></p>
                    <ul>
                        <li>Heat losses in hot water piping can account for more than 30% of water heating energy. These can be reduced by optimizing the length of the hot water piping (minimizing end-use to storage tank distance) and properly insulating the storage tank and piping,.</li>
                        <li>Reduce construction costs and heating losses by locating the solar water heater in close proximity to the building.</li>
                        <li>Use jute rope for insulation as this is a cheap option.</li>
                        <li>Set water heater temperature to an optimum level.</li>
                    </ul>
                    <p>(Source- <a href="http://www.grihaindia.org/">GRIHA</a>)</p>
                    <p><strong>AKSHAYA URJA SHOPS </strong></p>
                    <p>The Program of establishment of Akshay Urja Shops (earlier Aditya Solar Shops) was continued during the year. Under the Program, Akshay Urja Shops are being established in each district to make renewable energy products easily available to the people and provide after sales and repair services. The Program is in operation through State Nodal Agencies (SNAs). Financial support in terms of soft loans from designated banks and a maximum of&nbsp;2.40 lakh as recurring grant/incentive for first two years of operation from Ministry is available for establishing such shops. Service Charge is also provided to SNAs. A total of 340 shops in 31 States / UTs, (including 113 Aditya Solar Shops) have been established under the scheme. During the year, 85 Akshay Urja Shops were supported in Andhra Pradesh, A&amp;N Islands, Assam, Bihar, Jharkhand, Madhya Pradesh &amp; Uttar Pradesh.</p>
