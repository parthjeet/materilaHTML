                    <hr class="section-padding">
                    <p class="h4"><strong><u>Alternatives:</u></strong></p>
                    <p>Use insulation with zero-ODP (ozone depletion potential) such as:</p>
                    <ol>
                        <li>HCFC-free rigid foam insulation,</li>
                        <li>Mineral fiber cellulose insulation,</li>
                        <li>glass fiber,</li>
                        <li>Wood fiber board,</li>
                        <li>Cork</li>
                        <li>Wool</li>
                        <li>Expanded (bead) polystyrene</li>
                        <li>Recycled newspaper, and jute and cotton.</li>
                    </ol>
                    <p>Avoid materials that do not inherently have a zero- ODP, such as polyurethane foams and polyisocyanurates.</p>
                    <p>Install HVAC and refrigeration equipment that uses refrigerants that comply with the following requirement:</p>
                    <p class="jumbotron text-center"><strong>LCGWP + LCODP x 100,000 &lt; 100</strong></p>
                    <p>Where:</p>
                    <div class="table-responsive">
                    <table class="table table-bordered">
                        <tbody>
                        <tr>
                            <td class="text-center align-middle">
                                <p class="align-middle font-weight-bold">LCODP = [ODPr x (Lr x Life +Mr) x Rc]/Life</p>
                            </td>
                            <td>
                                <p>Where, LCODP: Lifecycle Ozone Depletion Potential (lbCFC11/Ton-Year)</p>
                                <p>ODPr: Ozone Depletion Potential of Refrigerant 0 &lt; ODPr &lt; 0.2 lbCFC11/lbr</p>
                                <p>Lr: Refrigerant Leakage Rate (% of charge per year) 0.5% &lt; Lr &lt; 3%/Year</p>
                                <p>Mr: End-of-life Refrigerant Loss (% of charge) 2% &lt; Mr &lt; 10 %</p>
                                <p>Rc: Refrigerant Charge (lbs refrigerant per Ton of cooling capacity) 0.9 &lt; Rc &lt; 3.3</p>
                                <p>Life: Equipment Life (Years) 10 &lt; Life &lt; 35 Years</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p class="font-weight-bold">LCGWP = [GWPr x (Lr x Life +Mr) x Rc]/Life</p>
                            </td>
                            <td>
                                <p>Where, LCGWP: Lifecycle Direct Global Warming Potential (lbCO2/Ton-Year)</p>
                                <p>GWPr: Global Warming Potential of Refrigerant (0 &lt; GWPr &lt; 12,000 lbCO2/lbr)</p>
                            </td>
                        </tr>
                        </tbody>
                    </table></div>
                    <hr class="section-padding">
                    <p>For projects with multiple units of base building level HVAC and refrigeration equipment, a weighted average of all base building level HVAC and refrigeration equipment shall be applied to the formula above:</p>
                    <div class="jumbotron">
                    <p class="text-center font-weight-bold"><strong>[SUM (LCGWP + LCODP x 10</strong><strong>5</strong><strong>) x Q unit] / Q total &lt; 100</strong></p>
