                    <p class="padding-left-2">Substances containing chlorine (or bromine) contribute to the breakdown of the ozone layer in the stratosphere, resulting in harmful UV radiation reaching earth&rsquo;s surface, and thus contributing to global climate change. Such substances are mainly used in refrigerating and air-conditioning equipment, fire suppression systems and extinguishers, and in insulation. This has been a growing cause for concern. Therefore, continued efforts are being made globally (in the form of international agreements) to minimize the use of ozone depleting substances, and gradually to replace them with environmentally friendly substances. Trichlorofluoromethane (R11) is used as reference for measuring the ODP (ozone depleting potential) of a substance. The ODP of R11 is 1. The OPD of some of the commonly used substances in refrigerating and air-conditioning equipment are listed in the Table</p>
