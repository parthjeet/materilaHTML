                    <tr>
                        <td>
                            <p>Noise caused by impact is the most intense and widespread of all industrial noises. It is normally coupled with resonant response of the structural members connected to the impacting surface. Common sources of this type of noise are forging, riveting, chipping, pressing, tumbling, cutting, weaving, etc. Intense impact noise may also be produced during handling of materials as in the case of sheared steel plates falling one over another in collecting trays in a steel factory. Impact noise is usually intermittent and impulsive in character, but it may also be continuous as in the case of tumbling.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>Friction</strong></p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p>Most of the noise due to friction is produced in such processes as sawing, grinding and sanding. Friction also occurs at the cutting edge on lathes and other machine tools and in brakes and from bearings. The spectrum of frictional noise often predominates in high frequency and is very unpleasant in character.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>Rotation and reciprocation</strong></p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p>A rotating or reciprocating machine generates noise due to unbalanced forces and/or pressure fluctuations in the fluids inside the machines. In many cases, the moving surfaces radiate noise directly and in other cases, the pressure fluctuations are transmitted to the outer casings of the machine from where they are radiated as noise. Interaction of rotating component with the fluid stream can also give rise to pure tone components, such as the whine in a turbine. Since most machine casings have radiation efficiencies of unity in the higher frequency range, the amount of sound radiated is often substantial.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>Air turbulence</strong></p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p>Noise may be generated by rapid variation in air pressure caused by turbulence from high velocity air, steam or gases. Common examples are the exhaust noise from pneumatic tools and air jets. The noise is intense, and broad based in character and the frequency criteria depends on the size of the jet. The intensity increases rapidly with the velocity of the air stream.</p>
                        </td>
                    </tr>
