                <div class="text-left">
                    <p><strong>Methods of Reducing Noise</strong></p>
                    <ol>
                        <li><strong>Noise Control by Location</strong></li>
                    </ol>
                    <p>Machines, processes and work areas which are approximately equally noisy should be located together as far as possible. Areas that are particularly noisy should be segregated from quiet areas by buffer zones that produce and may tolerate intermediate noise levels.</p>
                    <ol>
                        <li><strong>Noise Reduction by Layout</strong></li>
                    </ol>
                    <p>The office space in a factory should be as far as possible located preferably in a separate building. This building should not have a wall common with the production area. Where a common wall is unavoidable, it should be heavy with few connecting doors and no permanent openings.</p>
                    <p><strong>Noise Reduction at Source</strong></p>
                    <ol>
                        <li><strong>Selection of machinery</strong></li>
                    </ol>
                    <p>Noise should be reduced as near the source as possible. While the operational processes in a factory may be fixed and may have no quieter alternative, careful selection of the machine tools and equipment to be used may considerably help attaining lower noise levels in the machine shop.</p>
                    <ol>
                        <li><strong>Reducing noise from potential sources</strong></li>
                    </ol>
                    <p>Impact that is not essential to a process should be quietened. Noise from handling and dropping of materials on hard surface may be reduced by using soft resilient materials on containers, fixing rubber tyres on trucks, trolleys, etc. Machine noise may be kept to a minimum by proper maintenance. Proper lubrication will reduce noise by friction conveyors, rollers, etc.</p>
                    <p><strong>INDUSTRIAL ZONES</strong></p>
                    <p>In the case of sub-division of land in industrial zones of area 0.8 hectare or more, 5 percent of the total area shall be reserved as amenity open space which shall also serve as a general parking space; when such amenity open space exceeds 1 500 m2, the excess area could be utilized for the construction of buildings for banks, canteens, welfare centers and such other common purposes considered necessary for the industrial user, as approved by the Authority.</p>
