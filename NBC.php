                            <p>In industrial buildings of floor area less than 750 m<sup>2</sup> and used as low fire hazard occupancies, conventional ventilators fitted high up near the eaves of the external walls may serve as vents for smoke and hot gases, provided care is taken to ensure that they are kept open at all times or are designed to open automatically in case of fire.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>FIRE SAFETY</strong></p>
                            <p>Extinction of fires by closing the doors and windows is not likely in the case of industrial buildings because of their large size, where sufficient air to sustain the fire at least in the initial stages can be expected to be present.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>VENTILATION</strong></p>
                            <p>&middot; Of the two types of building ventilation, namely, vertical and horizontal, vertical ventilation is the one commonly adopted in the case of single storey industrial buildings.</p>
                            <p>&middot; In the case of industrial buildings wider than 30 m, the ventilation through windows may be augmented by roof ventilation.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>EXTERIOR WALL WINDOWS</strong></p>
                            <p>&middot; Exterior wall windows shall not be reckoned as satisfactory means for venting of fire gases and smoke in industrial buildings. However, they may be reckoned as additional means of venting when, they are located close to the eaves and are provided with ordinary glass or movable sash arranged for both manual and automatic operation.</p>
                            <p>&middot; In case of industrial buildings, the window height should be about 1.6 m and width about two-third of wall width. These should be located at a height of 1.1 m above the floor. In addition to this, openings around 0.9 m high should be provided over two-third length of the glazed area in the roof lights.</p>
                        </td>
                    </tr>
