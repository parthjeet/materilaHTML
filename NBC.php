                            <p><strong>ROOF SCREENS OR CURTAIN BOARDS</strong></p>
                            <p>Industrial buildings with large areas and having no sub-division/separating walls limiting the area of individual compartments to 750 m<sup>2</sup> or less, shall be provided with roof screens or curtain boards.</p>
