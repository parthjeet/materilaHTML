                            <p>Safety devices like industrial safety helmets conforming to the accepted standards and goggles made of celluloid lens, shall be issued to the workmen. Foreman-in-charge of the work areas shall ensure that all the workmen are wearing the safety devices before commencing any work.</p>
                        </td>
