                    <hr class="section-padding">
                    <p class="h4 text-center"><strong>GUIDELINES FOR SANITATION AND HOUSE KEEPING AT THE LABOUR /CONSTRUCTION CAMPS </strong></p>
                    <div class="table-responsive">
                        <table class="table table-bordered table-striped-green">
                        <tbody>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong>Sl. No.</strong></p>
                            </td>
                            <td class="text-center align-middle">
                                <p><strong>Guidelines</strong></p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>1.</p>
                            </td>
                            <td>
                                <p><strong>SITE SELECTION</strong></p>
                                <p>&middot; The construction camps will be located at least 200 - 500 m away from habitations at identified sites. The living accommodation and ancillary facilities for labour shall be erected and maintained to standards and scales approved by the resident engineer.</p>
                                <p>&middot; All sites used for camps must be adequately drained. They must not be subject to periodic flooding, nor located within 200 feet of swamps, pools, sink holes or other surface collections of water unless such water surface can be subjected to mosquito control measures.&nbsp;</p>
                                <p>&middot; The camps must be located such that the drainage from and through the camps will not endanger any domestic or public water supply.&nbsp;</p>
                                <p>&middot; All sites must be graded, ditched and rendered free from depressions such that water may get stagnant and become a nuisance.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>2.</p>
                            </td>
                            <td>
                                <p><strong>WATER SUPPLY </strong></p>
                                <p>&middot; An adequate and convenient water supply, approved by the appropriate health authority, must be provided in each camp for drinking, cooking, bathing and laundry purposes.</p>
                                <p>&middot; Potable water supply systems for labour camps occupants shall be as per the design approved by the Local Public Health Engineering Department and meet the water quality standards as prescribed by the State Pollution Control Board. In addition, the design of water system facilities shall be based on the suppliers Engineer&rsquo;s estimates of water demands.</p>
                                <p>&middot; The drinking water system must be monitored in accordance with the water quality parameters as prescribed by the State Pollution Control Board. The water supply system used for cooking purposes that is drained seasonally must be cleaned, flushed, and disinfected prior to use. Furthermore, a water sample of satisfactory bacteriologic quality, i.e. a sample showing not more than one coliform bacteria per 100 ml sample must be obtained before being placed into service.</p>
                                <p>&middot; At all construction camps and other workplace, good and sufficient water supply shall be maintained to eliminate chances of waterborne/water-related/water-based diseases to ensure the health and hygiene of the workers.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>3.</p>
                            </td>
                            <td>
                                <p><strong>TOILET FACILITIES AND HYGIENE</strong>&nbsp;</p>
                                <p>&middot; There shall be adequate supply of water, close to latrines and urinals.</p>
                                <p>&middot; Within the precincts of every workplace, latrines and urinals shall be provided in an accessible place, and the accommodation, separately for each of these, as per standards set by the Building and other Construction Workers (regulation of Employment and Conditions of Service) Act, 1996. Except in workplaces provided with water-flushed latrines connected with a water borne sewage system, all latrines shall be provided with dry-earth system (receptacles) which shall be cleaned at least four times daily and at least twice during working hours and kept in a strict sanitary condition. Receptacles shall be tarred inside and outside at least once a year.</p>
                                <p>&middot; Toilet facilities adequate for the capacity of the camp must be provided. Each toilet room must be located so as to be accessible, without any individual passing through any sleeping room.&nbsp;</p>
                                <p>&middot; A toilet room must be located within 200 feet of the door of each sleeping room. No toilet may be closer than 100 feet to any sleeping room, lunch area or kitchen.</p>
                                <p>&middot; Where the toilet rooms are shared, such as in multifamily shelters and in barracks type facilities, separated toilet rooms must be provided for each sex. These rooms must be distinctly marked &ldquo;for men&rdquo; and &ldquo;for women&rdquo; by signs printed in English and in the native language of the persons occupying the camp, or marked with easily understood pictures or symbols. If the facilities for each sex are in the same building, they must be separated by solid walls or partitions extending from the floor to the roof or ceiling.&nbsp;</p>
                                <p>&middot; Urinals must be provided on the basis of one unit or 2 linear feet of urinal trough for each 25 men. The floor from the wall and for a distance not less than 15 inches measured from the outward edge of the urinals must be constructed of materials impervious to moisture. Where water under pressure is available, urinals must be provided with an adequate water flush. Urinals troughs in privies must drain freely into the pit or vault, and the construction of this drain must be such as to exclude flies and rodents from the pit.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>4.</p>
                            </td>
                            <td>
                                <p><strong>WASTE DISPOSAL </strong></p>
                                <p>&middot; The sewage system for the camp must be designed, built and operated to the satisfaction of the concerned local State Govt. Department so that no health hazard occurs and no pollution to the air, ground or adjacent watercourse takes place. Compliance with the relevant legislation must be strictly adhered to.&nbsp;</p>
                                <p>&middot; Garbage bins must be provided in the camps and regularly emptied and the garbage disposed off in a hygienic manner to the satisfaction of relevant norms.</p>
                                <p>&middot; Unless otherwise arranged for by the local sanitary authority, arrangement for disposal of excreta by incineration at the workplace shall be made by means of a suitable incinerator approved by the local medical health or municipal authorities. Alternatively, excreta may be disposed off by putting a layer of night soils at the bottom of permanent tank prepared for the purpose and covering it with 15 cm layer of waste or refuse and then covering it with a layer of earth for a fortnight (by then it will turn into manure).&nbsp;</p>
                                <p>&middot; On completion of the works, all such temporary structures shall be cleared away, all rubbish burnt, excreta tank and other disposal pits or trenches filled in and effectively sealed off and the outline site left clean and tidy, at the Contractor&rsquo;s expense, to the entire satisfaction of the engineer.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>5.</p>
                            </td>
                            <td>
                                <p><strong>FIRST AID </strong></p>
                                <p>&middot; Injuries might occur during the construction period. It is therefore pertinent to provide first&middot; aid facilities for all the construction workers. At construction camps and at all workplaces first aid equipment and nursing staff must be provided. Since many of the workplaces may be far away from regular hospitals, an indoor health unit having one bed facility every 250 workers needs to be provided.&nbsp;</p>
                                <p>&middot; Adequate transport facilities for moving the injured persons to the nearest hospital must also be provided in ready to move condition.&nbsp;</p>
                                <p>&middot; The first-aid units should apart from an adequate supply of sterilized dressing material should contain other necessary appliances as per the factory rules of Uttar Pradesh.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p>6.</p>
                            </td>
                            <td>
                                <p><strong>MAINTENANCE </strong></p>
                                <p>&middot; All buildings, rooms and equipment and the grounds surrounding them shall be maintained in a clean and operable condition and be protected from rubbish accumulation.&nbsp;</p>
                                <p>&middot; All necessary means shall be employed to eliminate and control any infestations of insects and rodents within all parts of any labor camp. This shall include approved screening or other control of outside openings in structures intended for occupancy or food service facilities.</p>
                                <p>&middot; Each structure made available for occupancy shall be of sound construction, shall assure adequate protection against weather, and shall include essential facilities to permit maintenance in a clean and operable condition.</p>
                                <p>&middot; Comfort and safety of occupants shall be provided for by adequate heating, lighting, ventilation or insulation when necessary to reduce excessive heat. Each structure made available for occupancy shall comply with the requirements of the Uniform Building Code. This shall not apply to tent camps.</p>
                            </td>
                        </tr>
