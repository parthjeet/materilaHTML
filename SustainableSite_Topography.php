                <p class="h5"><strong>Natural Topography and/or Vegetation</strong></p>
                <p class="text-justify">Avoid disturbance to the site by retaining natural topography (and/ or) design vegetated spaces on the ground, for at least 15% of the site area.</p>
                <ul class="text-justify">
                    <li>Retaining &lsquo;Natural Topography&rsquo; in its broad sense means preserving the natural features of the terrain such as exposed natural rocks, water body, etc., &bull;</li>
                    <li>Vegetation on the ground shall only be considered; vegetation over built structures such as roofs, basement, podiums, etc., shall not be considered.</li>
                    <li>Grass medians, grass pavers, jogging track, open-air theatre, parking areas, driveways, walkways, playground, swimming pool, etc., are considered as site disturbances.</li>
                    <li>Only native / adaptive vegetation shall be considered.</li>
                    <li>Potted plants shall not be considered as vegetation.</li>
                    <li>Artificial vegetation shall not be considered.</li>
                </ul>
                <p><strong>OR</strong></p>
                <p class="h5"><strong>Vegetation over Built Structures</strong></p>
                <p class="text-justify">Restore disturbed site area by designing vegetated spaces over built structures and on the ground, for at least 30% of the site area (including development footprint).</p>
                <ul class="text-justify">
                    <li>Development footprint includes building footprint and other hardscapes areas such as parking, footpaths, walkways, roads, grass medians, grass pavers, etc.,</li>
                    <li>Vegetation on the ground as well as vegetation over built structures such as roofs, basement, podiums, etc., can be considered.</li>
                    <li>Partially vegetated areas and disturbed site areas such as grass pavers, grass medians, jogging track, open-air theatre, playground, is considered as site disturbances and shall not be considered.</li>
