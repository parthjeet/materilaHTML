                                    commercial buildings having a connected load of 100kW or contract demand of 120 KVA
                                    and above. While the Central Government has powers under the EC Act 2001, the state
                                    governments have the flexibility to modify the code to suit local or regional needs
                                    and notify them. The ECBC defines norms of energy performance and takes into
                                    consideration the climatic regions of the country where the building is located. The
                                    major components of the building which are being addressed through the code are:</p>
