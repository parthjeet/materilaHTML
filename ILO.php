                    <li><strong><u>Protection of Workers' Claims (Employer's Insolvency) Convention, 1992 (No. 173) and Recommendation, 1992 (No. 180)</u></strong>
                        <ul>
                            <li><strong>Definitions and methods of application</strong></li>
                        </ul>
                    </li>
                </ol>
                <ul>
                    <li>For the purposes of this Recommendation, the term insolvency refers to situations in which, in accordance with national law and practice, proceedings have been opened relating to an employer's assets with a view to the collective reimbursement of its creditors.</li>
                    <li>For the purposes of this Recommendation, Members may extend the term "insolvency" to other situations in which workers' claims cannot be paid by reason of the financial situation of the employer, and in particular to the following:
                        <ul>
                            <li>where the enterprise has closed down or ceased its activities or is voluntarily wound up;</li>
                            <li>where the amount of the employer's assets is insufficient to justify the opening of insolvency proceedings;</li>
                            <li>where, in the course of proceedings to recover a worker's claim arising out of employment, it is found that the employer has no assets or that these are insufficient to pay the debt in question;</li>
                            <li>where the employer has died and his or her assets have been placed in the hands of an administrator and the amounts due cannot be paid out of the estate.</li>
                        </ul>
                    </li>
                    <li>The extent to which an employer's assets are subject to the proceedings referred to in subparagraph (1) should be determined by national laws, regulations or practice.</li>
                </ul>
                <p>&nbsp;</p>
                <ul>
                    <li><strong>Protection of workers' claims by means of a privilege</strong></li>
                    <li>The protection afforded by a privilege should cover the following claims:
                        <ul>
                            <li>wages, overtime pay, commissions and other forms of remuneration relating to work performed during a prescribed period prior to the insolvency or prior to termination of the employment. This period should be fixed by national laws or regulations and should not be less than 12 months;</li>
                            <li>holiday pay due as a result of work performed during the year in which the insolvency or the termination of the employment occurred, and in the preceding year;</li>
                            <li>amounts due in respect of other types of paid absence, end-of-year and other bonuses provided for by national laws or regulations, collective agreements or individual contracts of employment, relating to a prescribed period, which should not be less than 12 months, prior to the insolvency or prior to the termination of the employment;</li>
                            <li>payments due in lieu of notice of termination of employment;</li>
                            <li>severance pay, compensation for unfair dismissal and other payments due to workers upon termination of their employment;</li>
                            <li>compensation payable directly by the employer in respect of occupational accidents and diseases.</li>
                        </ul>
                    </li>
                    <li>The protection afforded by a privilege might cover the following claims:
                        <ul>
                            <li>contributions due in respect of national statutory social security schemes, where failure to pay adversely affects workers' entitlements;</li>
                            <li>contributions due in respect of private, occupational, inter-occupational or enterprise social protection schemes independent of national statutory social security schemes, where failure to pay adversely affects workers' entitlements;</li>
                            <li>benefits to which the workers were entitled prior to the insolvency by virtue of their participation in enterprise social protection schemes and which are payable by the employer.</li>
                            <li>Claims enumerated in subparagraphs (1) and (2) that have been awarded to a worker through an adjudication or arbitration within 12 months prior to the insolvency should be covered by the privilege regardless of the time-limits specified in those subparagraphs.</li>
                        </ul>
                    </li>
                    <li><strong>Limitations</strong>
                        <ul>
                            <li>Where the amount of the claim protected by a privilege is limited by national laws or regulations, in order that this amount should not fall below a socially acceptable level it should take into account variables such as the minimum wage, the part of the wage which is unattachable, the wage on which social security contributions are based or the average wage in industry.</li>
                        </ul>
                    </li>
                    <li><strong>Claim which fall due after the insolvency proceedings have been opened</strong>
                        <ul>
                            <li>Where, in accordance with national laws and regulations, an enterprise in respect of which insolvency proceedings have been opened is authorized to continue its activities, workers' claims arising out of work performed as from the date when the continuation was authorized should not be subject to the proceedings and should be paid, out of the funds available, as and when they fall due.</li>
                        </ul>
                    </li>
                    <li><strong>Accelerated payment procedures</strong>
                        <ul>
                            <li>Where the insolvency proceedings cannot ensure rapid payment of workers' privileged claims, there should be a procedure for accelerated payment to ensure that the claims are paid, without awaiting the end of the proceedings, out of available funds or as soon as funds become available, unless the rapid payment of workers' claims is ensured by a guarantee institution.</li>
                            <li>Accelerated payment of workers' claims may be ensured as follows:
                                <ul>
                                    <li>the person or institution responsible for administering the employer's assets should pay such claims as soon as it has been determined that they are genuine and payable;</li>
                                    <li>if the claim is contested, the worker should be able to have its validity determined by a court or any other body with jurisdiction over the matter, so as to have it paid in accordance with clause (a). The accelerated payment procedure should cover the totality of the claim protected by a privilege, or at least a part of it to be fixed by national laws or regulations.</li>
                                </ul>
                            </li>
                        </ul>
                    </li>
                </ul>
