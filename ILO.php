                <ol start="2">
                    <li><strong><u>Minimum Wage Fixing Convention, 1970 (No. 131) and Recommendation, 1970 (No. 135)</u></strong>
                        <ul>
                            <li><strong>Purpose of minimum wages fixing</strong></li>
                        </ul>
                    </li>
                </ol>
                <ul>
                    <li>Minimum wage fixing should constitute one element in a policy designed to overcome poverty and to ensure the satisfaction of the needs of all workers and their families.</li>
                    <li>The fundamental purpose of minimum wage fixing should be to give wage earners necessary social protection as regards minimum permissible levels of wages.</li>
                </ul>
                <p>&nbsp;</p>
                <ul>
                    <li><strong>Criteria for Determining the Level of Minimum Wages</strong>
                        <ul>
                            <li>In determining the level of minimum wages, account should be taken of the following criteria, amongst others:
                                <ul>
                                    <li>the needs of workers and their families;</li>
                                    <li>the general level of wages in the country;</li>
                                    <li>the cost of living and changes therein;</li>
                                    <li>social security benefits;</li>
                                    <li>the relative living standards of other social groups;</li>
                                    <li>economic factors, including the requirements of economic development, levels of productivity and the desirability of attaining and maintaining a high level of employment.</li>
                                </ul>
                            </li>
                            <li><strong>Coverage of the Minimum Wage Fixing system</strong>
                                <ul>
                                    <li>The number and groups of wage earners who are not covered in pursuance of Article 1 of the Minimum Wage Fixing Convention, 1970, should be kept to a minimum.</li>
                                    <li>The system of minimum wages may be applied to the wage earners covered in pursuance of Article 1 of the Convention either by fixing a single minimum wage of general application or by fixing a series of minimum wages applying to particular groups of workers.</li>
                                    <li>A system based on a single minimum wage:</li>
                                    <li>
                                        <ul>
                                            <li>need not be incompatible with the fixing of different rates of minimum wages in different regions or zones with a view to allowing for differences in costs of living;</li>
                                            <li>should not impair the effects of decisions, past or future, fixing minimum wages higher than the general minimum for particular groups of workers.</li>
                                        </ul>
                                    </li>
                                    <li><strong>Minimum Wage Fixing Machinery</strong>
                                        <ul>
                                            <li>The minimum wage fixing machinery may take a variety of forms, such as the fixing of minimum wages by:
                                                <ul>
                                                    <li>statute;</li>
                                                    <li>decisions of the competent authority, with or without formal provision for taking account of recommendations from other bodies;</li>
                                                    <li>decisions of wages boards or councils;</li>
                                                    <li>industrial or labour courts or tribunals; or</li>
                                                    <li>giving the force of law to provisions of collective agreements.</li>
                                                </ul>
                                            </li>
                                            <li>The consultation provided should include, in particular, consultation in regard to the following matters:
                                                <ul>
                                                    <li>the selection and application of the criteria for determining the level of minimum wages;</li>
                                                    <li>the rate or rates of minimum wages to be fixed;</li>
                                                    <li>the adjustment from time to time of the rate or rates of minimum wages;</li>
                                                    <li>problems encountered in the enforcement of minimum wage legislation;</li>
                                                    <li>the collection of data and the carrying out of studies for the information of minimum wage fixing authorities</li>
                                                </ul>
                                            </li>
                                            <li>In countries in which bodies have been set up which advise the competent authority on minimum wage questions, or to which the government has delegated responsibility for minimum wage decisions, the participation in the operation of minimum wage fixing machinery should include membership of such bodies.</li>
                                            <li>The persons representing the general interests of the country whose participation in the operation of minimum wage fixing machinery should be suitably qualified independent persons who may, where appropriate, be public officials with responsibilities in the areas of industrial relations or economic and social planning or policy-making.</li>
                                        </ul>
                                    </li>
                                    <li><strong>Adjustment of Minimum Wages</strong>
