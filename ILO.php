                <p><strong>Working hours</strong><strong>- </strong>The principle of the eight-hour day and 48-hour week, as maximum normal working hours has been internationally established since 1919, when the first ILO Convention was adopted. Since then, the ILO has adopted another 21 Conventions, 18 Recommendations and one Protocol on working time.</p>
                <p>ILO standards on working time most applicable to the TCLF sector:</p>
                <ol>
                    <li><strong>Hours of Work (Industry) Convention, 1919 (No. 1)</strong>
                        <ul>
                            <li>the actual working hours of persons of fifteen years of age or over in any public or private industrial undertaking, or in any branch thereof, shall not exceed fifty-seven in the week</li>
                            <li>a weekly rest period of twenty-four consecutive hours shall be allowed to all classes of workers</li>
                        </ul>
                    </li>
                </ol>
                <p>&nbsp;</p>
                <ol start="2">
                    <li><strong>Weekly Rest (Industry) Convention, 1921 (No. 14)</strong>
                        <ul>
                            <li>where the weekly rest is given to the whole of the staff collectively, to make known such days and hours of collective rest by means of notices posted conspicuously in the establishment or any other convenient place, or in any other manner approved by the Government</li>
                            <li>where the rest period is not granted to the whole of the staff collectively, to make known, by means of a roster drawn up in accordance with the method approved by the legislation of the country, or by a regulation of the competent authority, the workers or employees subject to a special system of rest, and to indicate that system.</li>
                        </ul>
                    </li>
                </ol>
                <p>&nbsp;</p>
                <ol start="3">
                    <li><strong>Forty-Hour Week Convention, 1935 (No. 47)</strong>
                        <ul>
                            <li>the principle of a forty-hour week applied in such a manner that the standard of living is not reduced in consequence;</li>
                        </ul>
                    </li>
                </ol>
                <p>&nbsp;</p>
                <ol start="4">
                    <li><strong>Holidays with Pay Convention (Revised), 1970 (No. 132)</strong></li>
                </ol>
                <ul>
                    <li>Every person to whom this Convention applies shall be entitled to an annual paid holiday of a specified minimum length.</li>
                    <li>Each Member which ratifies this Convention shall specify the length of the holiday in a declaration appended to its ratification.</li>
                    <li>The holiday shall in no case be less than three working weeks for one year of service.</li>
                    <li>Each Member which has ratified this Convention may subsequently notify the Director-General of the International Labour Office, by a further declaration, that it specifies a holiday longer than that specified at the time of ratification.</li>
                    <li>A person whose length of service in any year is less than that required for the full entitlement shall be entitled in respect of that year to a holiday with pay proportionate to his length of service during that year.</li>
                    <li>A minimum period of service may be required for entitlement to any annual holiday with pay.</li>
                    <li>The length of any such qualifying period shall be determined by the competent authority or through the appropriate machinery in the country concerned but shall not exceed six months.</li>
                </ul>
                <p>&nbsp;</p>
                <ol start="5">
                    <li><strong>Reduction of Hours of Work Recommendation, 1962 (No. 116)</strong>
                        <ul>
                            <li>Normal hours of work should be progressively reduced, when appropriate, with a view to attaining the social standard indicated in the Preamble of this Recommendation without any reduction in the wages of the workers as at the time hours of work are reduced.</li>
                            <li>Where the duration of the normal working week exceeds forty-eight hours, immediate steps should be taken to bring it down to this level without any reduction in the wages of the workers as at the time hours of work are reduced.</li>
                            <li>Where normal weekly hours of work are either forty-eight or less, measures for the progressive reduction of hours of work should be worked out and implemented in a manner suited to the particular national circumstances and the conditions in each sector of economic activity.</li>
                        </ul>
                    </li>
                </ol>
                <p>&nbsp;</p>
                <ol start="6">
                    <li><strong>Night Work Convention, 1990 (No. 171) and its Recommendation, 1990 (No. 178)</strong></li>
                </ol>
                <ul>
                    <li>the term night work means all work which is performed during a period of not less than seven consecutive hours, including the interval from midnight to 5 a.m., to be determined by the competent authority after consulting the most representative organisations of employers and workers or by collective agreements;</li>
                    <li>the term night worker means an employed person whose work requires performance of a substantial number of hours of night work which exceeds a specified limit. This limit shall be fixed by the competent authority after consulting the most representative organizations of employers and workers or by collective agreements.</li>
                    <li>Specific measures required by the nature of night work, which shall include, as a minimum shall be taken for night workers in order to protect their health, assist them to meet their family and social responsibilities, provide opportunities for occupational advancement, and compensate them appropriately. Such measures shall also be taken in the fields of safety and maternity protection for all workers performing night work.</li>
                    <li>Night workers certified, for reasons of health, as unfit for night work shall be transferred, whenever practicable, to a similar job for which they are fit.</li>
                    <li>If transfer to such a job is not practicable, these workers shall be granted the same benefits as other workers who are unable to work or to secure employment.</li>
                    <li>A night worker certified as temporarily unfit for night work shall be given the same protection against dismissal or notice of dismissal as other workers who are prevented from working for reasons of health.</li>
                </ul>
                <p>&nbsp;</p>
                <p><strong>Key principles on working-time arrangements</strong></p>
                <p><strong>■ </strong>Need for daily, weekly and perhaps annual limits on working hours</p>
                <p>■ Importance of keeping overtime exceptional, limiting the number of additional hours and providing adequate compensation</p>
                <p>■ Right to regular and uninterrupted weekly rest</p>
                <p>■ Right to paid annual leave</p>
                <p>■ Need to keep night-time work exceptional and warranting special protection</p>
                <p>■ Importance of enterprises&rsquo; needs in respect of flexible working-time arrangements</p>
                <p>■ Right to collective bargaining and the full and genuine consultation of employers&rsquo; and workers&rsquo; representatives on working-time regulation</p>
                <p>■ Need for an effective labour inspection system or other enforcement measures to prevent and punish abusive practices</p>
                <p>&nbsp;</p>
                <p><strong>Labour inspection/administration</strong></p>
                <ul>
                    <li>The main responsibility for the implementation of wages and working-time legislation lies with governments. Ensuring compliance with national law requires labour administration and inspection systems that have efficient and effective methods of governance and management and can establish broad partnerships. Strengthening workplace compliance through labour inspection is one of the ILO&rsquo;s eight areas of critical importance recently established by the Governing Body.</li>
                </ul>
                <p>&nbsp;</p>
                <ul>
                    <li>Labour inspection and administration systems operate in a rapidly changing environment that is well exemplified by the TCLF industries: dramatic changes in production patterns, work organization, employment structures and relationships; labour migration; <br /> outsourcing; extended and complex global production systems; and the expansion of the informal economy. The 2011 ILC resolution concerning labour administration and labour inspection calls for sound labour inspection systems that can expand their coverage to all workers in an increasingly diverse global workforce, including workers in supply chains and export processing zones. The resolution supplements relevant ILO Conventions and Recommendations, which provide the main guidance for labour administration and inspection operations.</li>
                </ul>
                <p>&nbsp;</p>
                <ul>
                    <li>Compliance with minimum-wage and working-hours legislation remains a considerable challenge in the TCLF industries in many countries. Government action is required to improve working conditions in the TCLF industries, since governments have the legitimacy to enforce labour legislation and protect workers&rsquo; rights.</li>
                </ul>
                <p>&nbsp;</p>
                <p><strong>Collective bargaining agreements</strong></p>
                <p>Labour unrest in the TCLF industries in several countries has been linked to wages, hours of work and working conditions. In some cases, protests over grievances have turned violent, causing loss of life and severe injuries, while some prolonged protests have halted production and affected deliveries. Employers estimate significant losses in such cases, as on several occasions factories have been forced to close.</p>
