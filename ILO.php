                                            <li>Minimum wage rates should be adjusted from time to time to take account of changes in the cost of living and other economic conditions.</li>
                                            <li>To this end a review might be carried out of minimum wage rates in relation to the cost of living and other economic conditions either at regular intervals or whenever such a review is considered appropriate in the light of variations in a cost-of-living index.</li>
                                            <li>The frequency of such surveys should be determined in the light of national conditions.</li>
                                        </ul>
