                    <p class="h5"><strong><u>Water Efficient Technologies</u></strong></p>
                    <p><strong>Automatic Irrigation Timer</strong></p>
                    <ul>
                        <li>A simple-to-operate automatic timer or controller can be installed on an existing manual
                            irrigation system.
                        </li>
                        <li>The controller automatically will operate the sprinklers on the proper day of the week for
                            the correct amount of run time.
                        </li>
                        <li>This will meet the plant&rsquo;s water needs as well as apply the water in off-peak night or
                            early morning hours.
                        </li>
                        <li>More elaborate controllers offer extra flexibility to manage larger sites with many
                            different hydro zones and site conditions.
                        </li>
                        <li>Any controller can use a rain or soil moisture sensor to prevent the sprinklers from
                            operating when natural precipitation has met the plants&rsquo; water needs.
                        </li>
                    </ul>
                    <p>&ldquo;<strong>Smart&rdquo; Controllers</strong></p>
                    <ul>
                        <li>Unlike traditional controllers, which are really just timers, &ldquo;smart&rdquo;
                            controllers work by monitoring and using information about site conditions (such as soil
                            moisture, rain, wind, slope, soil, plant type and more), and applying the right amount of
                            water based on those factors.
                        </li>
                        <li>These climates based &ldquo;smart&rdquo; controllers are available from many manufacturers
                            and the irrigation industry has created an evaluation program to set standards of
                            performance for this class of product.
                        </li>
                    </ul>
                    <p><strong>Centralized Irrigation Controllers</strong></p>
                    <ul>
                        <li>A central control system does not relieve the water manager from monitoring and adjusting
                            the equipment.
                        </li>
                        <li>It allows them to quickly adjust multiple controllers to the monthly or daily changes in
                            conditions that affect the water needs of the plants.
                        </li>
                        <li>These units can be economically justified for larger and multiple site operations.</li>
                    </ul>
                    <p><strong>PC Software</strong></p>
                    <ul>
                        <li>Software programs have been developed to assist the designer and water manager in the
                            analysis of the efficiency of an existing or newly-designed irrigation system.
                        </li>
                    </ul>
                    <p><strong>New Sprinkler Type</strong></p>
                    <ul>
                        <li>Install a multi-trajectory rotating stream sprinkler with high application uniformity.</li>
                        <li>This friction resistance head operates at lower pressures and fills the product range
                            between spray heads and rotors.
                        </li>
                    </ul>
                    <p><strong>Flow Control Nozzles</strong></p>
                    <ul>
                        <li>Sprinklers with a uniform application rate that use the lowest possible water pressure are
                            the goal.
                        </li>
                        <li>One method that helps achieve this goal is the use of flow-control nozzles.</li>
                        <li>Each sprinkler nozzle is equipped with a flow-control device that compensates for flow
                            changes and maintains a uniform pressure.
                        </li>
                        <li>This can be a desirable feature on slopes. The flow control also acts to reduce flow should
                            a sprinkler head be broken.
                        </li>
                    </ul>
                    <p><strong>Sensors</strong></p>
                    <p>Several types of sensors are available that take the human factor out of irrigation system
                        operation.</p>
                    <ol>
                        <li>Rain shut-offs</li>
                        <li>Freeze sensors</li>
                        <li>Wind sensors</li>
                        <li>Flow sensors</li>
                        <li>Soil moisture measurement</li>
                        <li>Weather stations</li>
                    </ol>
