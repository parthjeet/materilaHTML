                    <p class="h4 text-center"><strong><u>Best practices to reduce water usage for
                                landscaping</u></strong></p>
                    <ol>
                        <li><strong>Xeriscaping</strong>

                            <p>Xeriscape means the conservation of water and energy through creative landscaping. This
                                word is derived from the Greek word, &lsquo;Xeros&rsquo;, meaning dry. These plants can
                                live with little or no supplemental watering while some are even drought tolerant.
                                Following are some recommendations to reduce water usage for landscaping.</p>
                            <ul>
                                <li>Pant a mix of native shrubs and xeriscape plants.</li>
                                <li>Reduce the lawn area, and plant more trees that require no water.</li>
                                <li>Plant palm trees, which are xerophytic, such as Phoenix dactylifera and Yucca
                                    starlite.
                                </li>
                                <li>Use ground covers such as Asparagus sprengeri, which is succulent; Pandanus dwarf,
                                    which is xerophytic; and Bougainvillea, which is a climber.
                                </li>
                            </ul>
                        </li>

                        <li><strong>Drip irrigation</strong>

                            <p>Drip irrigation system or sub-surface drip irrigation system saves water as it avoids
                                loss of water due to run-off, deep percolation or evaporation</p>
                        </li>

                        <li><strong>Sprinkler irrigation</strong>

                            <p>Sprinkler irrigation is a method similar to natural rainfall in which water is
                                distributed through a system of pipes. For maintaining uniform distribution of water,
                                the pump supply system, sprinklers, and operating conditions must be designed
                                appropriately. Sprinklers are most suited to sandy soils with high infiltration rates.
                                The average application rate should be less than the basic infiltration rate of the soil
                                so as to avoid surface ponding and run-off. It is better to use sprinklers that produce
                                fine sprays than those that produce larger water droplets.</p>
                        </li>
                        <li><strong>Native vegetation</strong>

                            <p>Native vegetation is original to a particular place, including trees, shrubs, and other
                                plants. Evapo-transpiration rate The PET (potential evapo-transpiration rate) is the
                                climate factor and refers to the amount of water required by a plant for its healthy
                                growth (depending on the climate). Evapo-transpiration rate determines the rate at which
                                plants lose water through evaporation. It is affected by humidity and temperature at a
                                given time. These rates vary with the season. The data is available with the Indian
                                Meteorological Department for each city and can be procured from the Additional Director
                                General of Meteorology (Research), Shivajinagar, Pune &ndash; 411 005.</p>
                        </li>
