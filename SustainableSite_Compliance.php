                                <p><strong>Forest Conservation Act, 1980</strong></p>
                            </td>
                            <td>
                                <p><strong>With the rising rate of deforestation and the resulting environmental degradation, the Central Government enacted the forest (conservation) Act in 1980. The Act prohibits the deletion of a reserved forest or the diversion of forestland for any non-forest purpose, and prevents the cutting of trees in a forest without the prior approval of the Central government. The salient features of the act are given below: </strong></p>
                                <p>&middot; This Act has five Sections which deal with conservation of forests.&nbsp;</p>
                                <p>&middot; The Act was enacted with the twin objectives under Section 2 of restricting the use of forest land for non-forest purposes, and preventing the de-reservation of forests that have been reserved under the Indian Forest Act, 1927. However, in 1988 the Act was further amended to include two new provisions under Section 2, where it sought to restrict leasing of forest land to private individuals, authority, corporations not owned by the Government, and to prevent clear felling of naturally grown trees.&nbsp;</p>
                                <p>&middot; The Act empowers the Central Government to constitute a committee to advise the Government with a grant of approval under Section 2, as also on any other matter connected with the conservation of forest and referred to it by the Central Government.</p>
                                <p>&middot; The Act provides for the punishment of imprisonment, extendable to fifteen days for the contravention of the provisions of the Act. The Act provides for punishment of offenders from the Government Departments, including Head of the Departments and authorities.</p>
                                <p>(Source- <a href="http://envfor.nic.in/legis/forest/forest2.html">Ministry of Environment, Forest and Climate Change</a>)</p>
