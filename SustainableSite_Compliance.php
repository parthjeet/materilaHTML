                            </td>
                            <td>
                                <p><strong>Central Government declared the following areas as CRZ and imposed restrictions on the setting up and expansion of industries, operations or processes and the like in the CRZ through the Coastal Regulation Zone Notification, 2011: </strong></p>
                                <p>&middot; The land area from High Tide Line (HTL means the line on the land up to which the highest water line reaches during the spring tide) to 500 meters on the landward side along the sea front.&nbsp;</p>
                                <p>&middot; CRZ shall apply to the land area between HTL to 100 meters or width of the creek whichever is less on the landward side</p>
                                <p>along the tidal influenced water bodies that are connected to the sea and the distance upto which development along such tidal influenced water bodies is to be regulated shall be governed by the distance up to which the tidal effects are experienced which shall be determined 58 based on salinity concentration of 5 Parts Per Thousand (ppt) measured during the driest period of the year and distance upto which tidal effects are experienced shall be clearly identified and demarcated accordingly in the Coastal Zone Management Plans (CZMPs).</p>
                                <p>&middot; The land area falling between the hazard line and 500 meters from HTL on the landward side, in case of seafront and between the hazard line and 100 meters&rsquo; line in case of tidal influenced water body the word &lsquo;hazard line&rsquo; denotes the line demarcated by Ministry of Environment and Forests (MoEF) through the Survey of India (SoI) taking into account tides, waves, sea level rise and shoreline changes.&nbsp;</p>
                                <p>&middot; Land area between HTL and Low Tide Line (LTL) is termed as the intertidal zone.&nbsp;</p>
                                <p>&middot; The water and the bed area between the LTL to the territorial water limit (12 Nm) in case of sea. The water and the bed area between LTL at the bank to the LTL on the opposite side of the bank, of tidal influenced water bodies.</p>
