                                <p><strong>Following provisions of The Cantonment Act, 2006 have been identified by URDPFI guidelines that have relevance to the preparation of Regional or Urban Development Plans and their implementation.</strong></p>
                                <p>1. The Central Government can declare any place or places along with boundaries in which the Forces is quartered or which is required for the service of such forces, to be a cantonment and can also declare that any such area shall cease to be a cantonment, by notification in the Official Gazette.</p>
                                <p>2. The Central Government can define the limits of any cantonment for the aforesaid purposes, by a like notification.</p>
                                <p>3. The Central Government can, by notification in the Official Gazette, direct that in any place declared a cantonment under this Act, the provisions of any enactment relating to local self-government other than this Act shall have effect only to such extent, as may be specified in the notification.</p>
