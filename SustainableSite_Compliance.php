                                <p>The Ancient Monuments and Archaeological Sites and Remains Act, 1958 was amended in 2010. This Act provides prohibited and restricted areas around protected monuments as a buffer in which construction activities are prohibited and restricted. According to the Act prohibited and restricted areas are defined as follow:</p>
                                <p>&nbsp;<strong><u>As per the section 20 A of the Act, Prohibited Areas:</u></strong> Every area, beginning at the limit of the protected area or the protected monument, as the case may be, and extending to a distance of one hundred meters in all directions shall be the prohibited area. Government of India can specify an area even more than hundred meters. Construction activities in such areas are prohibited.</p>
                                <p><strong><u>Section 20 B defines Regulated Area:</u></strong> Every area, beginning at the limit of the prohibited area in respect of ever ancient monument and archaeological site and remains, declared as of national importance, extending to a distance of two hundred meters in all directions shall be the regulated area in respect of such protected area or protected monument. Government of India can specify an area even more than two hundred meters. Construction activities in such areas are regulated.</p>
