                    <p><strong>Turf Grass</strong></p>
                    <p>Whenever possible, plant alternative groundcovers that require less water, or consider the use of patios and decks, further reducing water demand.</p>
                    <ol>
                        <li>Turf grass has the highest water consumption of any plant group.</li>
                        <li>Plant grass only where it will provide optimal functional and aesthetic benefits.</li>
                        <li>Avoid very small turf areas under 10 feet wide.</li>
                        <li>Proper watering of turf (less frequent and deeper vs. frequent and light watering) will promote deep root development, which will make the turf more drought resistant.</li>
                        <li>Turf grass should be cut to the maximum recommended height for its type, generally a minimum of two inches to a maximum of four inches for optimum water use.</li>
                    </ol>
                    <p><strong>To reduce Landscape Water Use</strong></p>
                    <ul>
                        <li>Limit the turf grass area to less than 20% of landscape area</li>
                        <li>Use drought tolerant species for a minimum 40% of the landscaped area</li>
                        <li>Use water efficient irrigation systems to reduce losses through evaporation</li>
                        <li>Landscape areas over built structures such as basements, podium, roofs, etc., can be considered for landscape area calculations.</li>
                        <li>Only vegetated areas will be considered for this credit.</li>
                        <li>Potted plants should not be considered under landscaping.</li>
                        <li>Turf and each type of bedding area must be segregated into independent zones based on watering needs.</li>
                        <li>At least 50% of landscape planting beds must have drip irrigation system</li>
