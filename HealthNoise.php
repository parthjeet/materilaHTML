                    <p><strong>NOISE CONTROL</strong></p>
                    <ol>
                        <li><strong><em> From Room Air Conditioners (RAC)</em></strong>

                        <p class="img-padding-left-right">The following measures should be adopted:</p>
                        <ul class="img-padding-left-right">
                            <li>Selection of RAC which has the least noise at various fan speeds.</li>
                            <li>Install it at a serviceable height.</li>
                            <li>Install preferably in a wall or on a rigid window.</li>
                            <li>Provide only necessary slope as specified byte manufacturer, to avoid any unusual noise
                                from the compressor because of tilting.
                            </li>
                            <li>Install it preferably in the middle portion of the wall/window to avoid additional
                                directivity (do not install at the end of a wall.)
                            </li>
                            <li>Ensure all leaks are sealed properly.</li>
                            <li>Avoid condenser facing any high noisy areas, such as road/factory to avoid any such
                                noise predominantly entering into the room.
                            </li>
                            <li>Do not provide any props at the back Sidebottom of the air conditioner unless specified
                                by the manufacturer.
                            </li>
                            <li>Prepare the opening to suit the chassis with wooden frame of adequate rigidity and
                                thickness.
                            </li>


                        </ul><br><br></li>


                        <li><strong><em> From Split Air Conditioner/Furred Inn</em></strong>

                        <p class="img-padding-left-right">The following measures should be adopted:</p>
                        <ul class="img-padding-left-right">
                            <li>Install the evaporator only on a rigid wall/ceiling or on a pedestal.</li>
                            <li>Avoid installation over wooden/gypsum board partition. Should a need arise anchor the
                                evaporator rigidly by using mild steel frame work from the roof to avoid vibration.
                            </li>
                            <li>Provide proper &lsquo;u&rsquo; trap in the condensate water line to ensure a good water
                                seal, which will also avoid sound penetration into the room from outside.
                            </li>
                            <li>If the capillary is in the evaporator, ensure that flow noise is avoided.</li>
                            <li>Ensure proper return air entry back to the coil, since blowers working at higher <em>static
                                    pressure </em>will create higher noise.
                            </li>
                            <li>Select the condensers with top/side discharge depending upon location to avoid nuisance
                                to neighbors.
                            </li>
                            <li>Place condensers on rigid platform, properly supported propped and fixed firmly.</li>
                            <li>Ensure all screws, bolts and nuts are firmly tightened since stiffening is more
                                advantageous in higher frequencies for vibration reduction.
                            </li>

                        </ul><br><br></li>

                        <li><strong><em> Air Handling Units (Floor Mounted and Ceiling Suspended)</em></strong>
                        <p class="img-padding-left-right">The following measures should be adopted:</p>
                        <ul class="img-padding-left-right">
                            <li>Selected indoor machine for specific air quantity and static pressure.</li>
                            <li>Suspend the indoor machine and ducts without touching the members of the false ceiling
                                or partitions.
                            </li>
                            <li>Ensure that ducts/duct supports do not touch the evaporator.</li>
                        </ul>
                        <br><br></li>

                        <li><strong><em> From Plenum Chamber</em></strong>

                        <p class="img-padding-left-right">The following measures should be adopted/considered:</p>
                        <ul class="img-padding-left-right">
                            <li>If possible and if pressures allow, expand them to a plenum chamber (of 2.5 m/s for
                                normal office), which is acoustically lined inside.
                            </li>
                            <li>Stiffening of the plenum body is very critical since it could create a drumming noise.
                            </li>
                            <li>Plenum chambers with sound absorbing material are frequently used as silencers in air
                                conditioning and ventilating systems and in testing facilities to reduce flow velocity
                                and turbulence. The attenuation of these devices may be due to both dissipative and
                                reactive effects.
                            </li>

                        </ul>
                        <br><br></li>
                        <li><strong><em> From Fans</em></strong>
                        <p class="img-padding-left-right">To reduce fan noise, the following should be adopted:</p>
                        <ul class="img-padding-left-right   ">
                            <li>Design the air distribution system for minimum resistance, since the sound generated by
                                a fan, regardless of type, increase by the square of the static pressure. Turbulence can
                                increase the flow noise generated by duct fittings and dampers in the air distribution
                                systems especially at low frequencies.
                            </li>
                            <li>Examine the specific sound power levels of the fan designs for any given job. Different
                                fans generate different levels of sound and produce different octave band spectra.
                            </li>
                            <li>Selecta fan that will generate the lowest possible sound level, commensurate with other
                                fans election parameters.
                            </li>
                            <li>Fans with relatively few blades (less than 15) tend to generate tones, which may
                                dominate the spectrum. These tones occur at the blade passage frequency and its
                                harmonies. The intensity of these tones depends on resonance with the duct system, fan
                                design, and inlet flow distortions.
                            </li>
                            <li>Select a fan to operate as near as possible to its rated peak efficiency when handling
                                the required quantity of air and static pressure. Also, select a fan that generates the
                                lowest
                            </li>
                            <li>possible noise but still meets the required design conditions for which it is selected.
                                Using an oversized or undersized fan, that does not operate at or near rated peak
                                efficiency, may result in substantially higher noise levels.
                            </li>
                            <li>Design duct connections at both the fan inlet and outlet for uniform and straight
                                airflow. Avoid unstable, gusting, and swirling inlet airflow. Deviation from accepted
                                applications can severely degrade both the aerodynamic and acoustic performance of any
                                fan and invalidate manufacturers ratings or other performance predictions.
                            </li>
                            <li>Select duct silencers that do not significantly increase the required fan total static
                                pressure.
                            </li>

                        </ul>
                        <br><br></li>
                        <li><strong><em> From Chillers, Pumps and Pipes</em></strong>
                        <p class="img-padding-left-right">Sizing and selecting a chiller is an important aspect in noise
                            control. The following guidelines may be considered for noise control:</p>
                        <ul class="img-padding-left-right">
                            <li>For roof top installation of chillers, these maybe placed on beams connected on the
                                elevated levels of pillars on correctly chosen vibration isolators.
                            </li>
                            <li>Water cooled chillers have less vibration. However, if air cooled chillers have to be
                                chosen, choose them with fan of less speeds and compressors must be jacketed without
                            </li>
                            <li>compromising their ventilation requirement.</li>
                            <li>If much more silencing is required, plan silencer on the exhaust of the fans and also an
                                acoustic enclosure around the chillers. Care must be taken for the additional static
                                demanding the fan.
                            </li>

                        </ul>
                        <br><br></li>
                        <li><strong><em> From Ducting Work</em></strong>
                        <p class="img-padding-left-right">The following measures should be adopted:</p>
                        <ul>
                            <li>Shorter ducts with flanges and bracings is very advantageous for noise reduction.</li>
                            <li>Choose the right thickness of sheets for ducting.</li>
                            <li>Provide calculated turning vanes in all bends.</li>
                            <li>Provide take off pieces in all branches and collars.</li>
                            <li>Minimize the number of terminals since each terminal of equal noise will create a higher
                                overall noise inside the room &mdash; Two equal noise source increase the noise by 3 dB.
                            </li>
                            <li>Velocities of supply and return ducts and also terminals are important for noise
                                control.
                            </li>
                            <li>For auditoriums, conference halls etc., choose the right silencers in the supply. Define
                                clear opening for return air and fix return air silencers (parallel baffle silencer).
                                The pressure drop expected across these silencers varies from 6 mm to 10 mm of water
                                column.
                            </li>
                            <li>Selecting double skin air handling unit should be done with care. If used without supply
                                and return air silencers it adds to the noise in the duct patch. However, by using
                                double skin air handling unit the noise inside the plant room can be lowered.
                            </li>
                            <li>Instead of insulating the plant room, increasing the density of the plant room wall and
                                providing return air baffles in the return air patch is more helpful in noise reduction.
                                The
                            </li>
                            <li>doors to the air handling unit room should be either with an attic entry or dense enough
                                to avoid noise transmission.
                            </li>
                            <li>Avoid terminal dampers and grilles if the noise criteria are of the order of NC
                                20(recording studios).
                            </li>
                            <li>If ducts have to be routed outside the conditioned space, the density of the insulating
                                materials over the duct surface is very critical. Higher the density, lower is its noise
                                transmitality and hence break in noise inside the duct can be avoided. The density is to
                                be decided based on the outside noise level.
                            </li>
                            <li>Selection of a proper terminal device helps in noise reduction.</li>
                            <li>VAV shall be planned along with relevant VFD or bypass arrangement. Otherwise the duct
                                is subjected to variable pressures resulting in variable noise pattern.
                            </li>
                            <li>Minimize flow-generated noise by elbows or duct branch takeoffs, whenever possible, by
                                locating them at least four to five duct diameters from each other. For high velocity
                                systems, it may be necessary to increase this distance to up to ten duct diameters in
                                critical noise areas.
                            </li>
                            <li>Keep airflow velocity in the duct as low as possible (7.5 m/s or less) near critical
                                noise areas by expanding the duct cross-section area. However, do not exceed an included
                                expansion angle of greater than 15&deg;. Flow separation, resulting from expansion
                                angles greater than 15&deg;, may produce rumble noise. Expanding the duct cross-section
                                area reduces potential flow noise associated with turbulence in these areas.
                            </li>
                            <li>Use turning vanes in large 90&deg; rectangular elbows and branch takeoffs. This provides
                                a smoother transmission in which the air can change flow direction, thus reducing
                                turbulence.
                            </li>
                            <li>Place grilles, diffusers and registers into occupied space as far as possible from
                                elbows and branch takeoffs.
                            </li>
                            <li>Minimize the use of volume dampers near grilles, diffusers and registers in acoustically
                                critical situations.
                            </li>
                            <li>Vibration isolate ducts and pipes, using spring and/or neoprene hangers for atleast the
                                first15 m from the vibration-isolated equipment.
                            </li>
                        </ul>
                        <br><br></li>
                        <li><strong> Structure Borne Noise</strong>

                        <p>Most obvious paths for solid-borne noise are the attached piping and pump support systems.
                            Oscillatory energy generated near the pump can be conducted as solid-borne noise for
                            substantial distances before it is radiated as acoustic noise. It can be controlled using
                            flexible couplings and mechanical isolation.</p></li></ol>
                        <hr class="section-padding">
                        <p><strong>INDUSTRIAL NOISES</strong></p>

                        <ul>
                            <li>Industrial buildings are primarily producers rather than receivers of noise. The level
                                of industrial noise commonly exceeds that from any other source with the exception of
                                aircraft. As compared with traffic noise, its effects are less widespread but it is
                                often more annoying in character.
                            </li>
                            <li>Many industrial noises contain very strong high frequency whines, screeches and clatter
                                &mdash; these components are relatively more attenuated by passage through the air and
                                by the insulation of light structure than are lower frequencies.
                            </li>
                            <li>Intermittent noises are either isolated explosions or reports, or noises of a periodic
                                nature, such as those of pressure relief valves of blow off, or the noises of work
                                occurring at random intervals, for example, hammering, grinding and sawing operations;
                                the latter class may be especially irritating because of high frequency components.
                            </li>
                        </ul>
                        <br>
                        <p><strong>Methods of Reducing Noise</strong></p>
                        <ol>
                            <li><strong><em>Noise Control by Location</em></strong>

                            <p>Machines, processes and work areas which are approximately equally noisy should be
                                located together as far as possible. Areas that are particularly noisy should be
                                segregated from quiet areas by buffer zones that produce and may tolerate intermediate
                                noise levels.</p></li>

                            <li><strong><em>Noise Reduction by Layout</em></strong>

                            <p>The office space in a factory should be as far as possible located preferably in a
                                separate building. This building should not have a wall common with the production
                                area.</p>
                            <p>Where a common wall is unavoidable, it should be heavy with few connecting doors and no
                                permanent openings.</p>
                            </li>
                            <li><strong><em>Noise Reduction at Source</em></strong>

                            <p>&nbsp;</p>
                            <p><strong>3.1 <em>Selection of machinery</em></strong></p>
                            <p>Noise should be reduced as near the source as possible. While the operational processes
                                in a factory may be fixed and may have no quieter alternative, careful selection of the
                                machine tools and equipment to be used may considerably help attaining lower noise
                                levels</p>
                            <p><strong>3.2 <em>Reducing noise from potential sources</em></strong></p>
                            <p>Impact that is not essential to a process should be quietened. Noise from handling and
                                dropping of materials on hard surface may be reduced by using soft resilient materials
                                on containers, fixing rubber tyres on trucks, trolleys, etc. Machine noise may be kept
                                to a minimum by proper maintenance. Proper lubrication will</p>
                            <p><strong>3.3 <em></em> Noise by friction conveyors, rollers, etc.</strong></em></p>
                            <p> The <em>noise from the radiating surfaces</em> maybe reduced by reducing the radiating
                                area. For example, if the area is halved, the noise intensity will be reduced by 3 dB
                                and at low frequencies the reduction will be much greater.</strong></p>
                            <p><strong>3.4 </strong>Supporting structures for vibrating machines and other equipment
                                should be frames rather than cabinets or sheeted enclosures. If an enclosure is used,
                                precaution should be taken to isolate it and line it on the inside with sound-absorbent
                                material. The noise radiated by machinery guards can be minimized by making them of
                                perforated sheet or of wire mesh.</p>
                            <p><strong>3.5 <em>Reducing transmission of mechanical vibration </em></strong></p>
                            <p>A vibrating sources does not usually contain a large radiating surface but the vibration
                                is conducted along mechanically rigid paths to surfaces that can act as effective
                                radiator. If the rigid connecting paths are interrupted by resilient materials, the
                                transmission of vibration and consequently the noise radiated may be greatly reduced.
                                The reduction depends on the ratio of the driving (forcing) frequency of the source to
                                the natural frequency of the resilient system.</p></li>
                        </ol>
