                    <p class="h4 text-center"><strong><u>RAINWATER HARVESTING SYSTEM</u></strong></p>
                    <p><strong>Design of capacity of a water harvesting </strong></p>
                    <p>The amount the amount of rainwater that can be collected in any detention basin, the following data is required:</p>
                    <ol>
                        <li>Rainfall data for the area under consideration from the local meteorological department for each month of the year.</li>
                        <li>Area of the catchment feeding rainwater to the basin</li>
                    </ol>
                    <p>Once the above data is available the basic calculation for any given period is:</p>
                    <p class="text-center jumbotron font-weight-bold"><u>Collection (in kiloliters) = Rainfall (in meters) x Catchment area (in square meters)</u></p>
                    <p><strong>Steps to be followed for estimating the capacity of the harvesting pond include:</strong></p>
                    <ul>
                        <li>Calculating the run roofs for each surface, namely roof, paving, gravel, forest, field, court and lawn according to the % of water withheld.</li>
                        <li>Computing the maximum possible rainwater collection for each month by multiplying the run-off factor with the total quantity of rainfall on the catchment</li>
                        <li>Computing the run off factor by integrating all the areas for the entire site</li>
                        <li>Determining the rate of evaporation for different months from the monthly temperature and air velocity data</li>
                        <li>Preventing water loss of water due to percolation from the base of the basin to the soil by lining the base with an impervious material</li>
                        <li>Estimating the water accumulated during each month.</li>
                    </ul>
                    <p><strong>Controlling waste of curing water</strong></p>
                    <p>The following are guidelines to avoid wastage of curing water.</p>
                    <ul>
                        <li>Curing chemicals should be sprayed on concrete structures and free flowing water should not be cured.</li>
                        <li>After curing on the first day, all concrete structures should be painted with curing chemicals. This will stop daily water curing and save water.</li>
                        <li>Water should be sprayed on concrete structures after covering them with cloth/gunny bags. This decreases water rebound and ensures sustained and complete curing.</li>
                        <li>Ponds should be made using cement and sand mortar to avoid water flowing away from the flat surface while curing.</li>
                        <li>Water ponding should be done on all sunken slabs. This also highlights the importance of having an impervious formwork.</li>
                    </ul>
