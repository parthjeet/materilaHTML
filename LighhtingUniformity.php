                    <p>&ldquo;Uniformity of lighting‟ can be desirable or less desirable depending on the function of the space and type of activities. Lighting standards and codes usually provide recommended illuminance ratios between the task area and its surroundings (EN 12464-1 2002) (CIBSE 1997) (IESNA 2000, 676708). Ideally the luminances surrounding a task should decrease gradually, thereby avoiding harsh contrasts.</p>
                    <ul>
                        <li>General lighting installations typically provide an approximately uniform illuminance over the whole of the working plane. Such systems are often based upon the lumen method of design, where an average illuminance is:</li>
                    </ul>
                     <div class="well img-padding-left-right text-center"> <p>(Equation 1)</p>
                         <P><u>Average illuminance (lux) = Luminous flux (lumen) &times; Utilization factor &times; Maintenance factor Area (m<sup>2</sup>)</u> </P>

                     </div>
                    <ul>
                        <li>It is recommended that the uniformity of illuminance over any task area and immediate surrounding should not be less than 0.8 (Casey 2003). When considering the lighting of a task area and its immediate surroundings, uniformity of illuminance must be considered (Smith 2010).</li>
                    </ul>

                    <div class="well img-padding-left-right text-center"> <p>(Equation 2)</p>
                        <P><u>Uniformity of Luminance = Minimum Illuminance /    Mean illuminance </u> </P>
                    </div>
                    <p>In many workplaces it is unnecessary to illuminate all areas to the same level. Localized or local lighting may provide some degree of energy saving, but whichever system is used the variance in illuminance across an interior must not be excessive. The diversity of illuminance is expressed as: </p>
                    <div class="well img-padding-left-right text-center"> <p>(Equation 3)</p>
                        <P><u>Diversity of illuminance = Maximum Illuminance /Minimum illuminance  </u> </P>
                    </div>
                    <p>The diversity of illuminance at any point in the „core area‟ must not exceed 5:1. The core area is that area of the working plane having a boundary 0.5 m from the walls (Casey 2003). </p>
                </div>
                <ul class="text-justify">
                    <li>Installations lit by ceiling-mounted arrays designed by the &bdquo;lumen method‟ following the usual layout and spacing criteria will normally satisfy the uniformity requirements. Layout should be based on &bdquo;Spacing to Height Ratio‟ (SHR) close to the maximum spacing to mounting height ratio at which this uniformity is achieved. (Casey 2003).</li>
                </ul>
                <br><br>
                <p class="text-justify">As a rule of thumb, to achieve uniform lighting requires the spacing between the luminaries to be no larger than 1.5 times the mounting height </p>
                <div class="well img-padding-left-right">
                    <P><u>Lumnaire Spacing = 1.5 × Hm (Casey 2003).  </u> </P>
                </div>
                <ul class="text-justify">
                    <li>
                        <p>The <strong>Spacing to Height Ratio&rsquo; </strong>(SHR or S/H) is the ratio of the centre-to-centre distance between adjacent luminaires, to their height above the working plane. It is used to estimate if lighting is uniform. For a rectangular arrangement of luminaires:</p>
                    </li>
                </ul>
                <div class="well img-padding-left-right text-center"> <p>(Equation 4)</p>
                    <P><u>SHR =(1/H )√ (A/N)  where, A = total floor area, N = number of luminaires  </u> </P>
                </div>

                <p class="text-justify">This enables the most economical lighting installation to be planned. In practice, the spacing may be closer to avoid shadowing or to increase the illumination. Considering visual comfort and visual performance of the user, the illuminance ratio at and around the task area should be uniform. Here, room surface reflectance is an important part of a lighting system that can help to ensure both the uniformity and energy usage of lighting. </p>
