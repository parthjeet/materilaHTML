                            <td class="text-center align-middle">
                                <p><strong>Contaminant source control</strong></p>
                            </td>
                            <td>
                                <p>Controlling the source of contaminants is fundamental to any IAQ strategy. Indoor contamination can also be in the form of particles or chemicals. They may come from building occupants and their activities, be emitted from furnishings and wall coverings, or be brought into the building with the intake air from outdoors. Controlling these contaminants at the source is typically a much more cost-effective strategy than filtering or diluting them once they are inside the building.</p>
                                <p><strong>Indoor Sources</strong></p>
                                <ul>
                                    <li> Contamination can originate inside the building or be brought in from outdoors. The most obvious indoor-generated contaminants are those created by the activities of the building occupants, such as cooking, smoking, photocopying, laser printing, and other processes.</li>
                                    <li> Low levels of <strong>VOCs</strong> originate from many different locations within the building, making local exhaust at the source difficult. In this situation, dilution with clean outdoor air is typically the most practical and cost-effective solution.</li>
                                    <li> <strong>Microbial contamination (fungi and bacteria)</strong> can also be a major source of indoor contamination. Microbiological colonies can grow in or on various building elements and furnishings, including carpets, ceilings, sheetrock walls, and within the HVAC system. Proper selection and specification of building materials and HVAC equipment are key places to start with while reducing potential microbial growth. Following methods can be used:</li>

                                </ul>
                                <p><em>Sloped, Noncorrosive Drain Pans:</em> condensate drain pans should be sloped, preferably in two directions, to assure positive drainage and eliminate standing water both when the system is operating and when it is idle. Furthermore, the drain pans should be constructed of noncorrosive materials, such as stainless or galvanized steel or polymers, to resist the deterioration that leads to premature leakage or porous surfaces.</p>
                                <p><em>Cleanability:</em> If infestations are found, these areas must be properly cleaned immediately and the cause of the contamination must be determined and corrected. The porous surface of the insulation can store dirt, creating a habitat for microbial growth if moisture is present. Once contaminated with mold, the porous insulation is almost impossible to clean short of removing and replacing it.</p>
                                <p><em>Accessibility:</em> cooling coils and drain pans must be readily accessible for inspection, cleaning, and maintenance in their normal operating position (in-situ). The equipment must be positioned and installed within the building to assure adequate access to all major components without substantial unit disassembly.</p>
                                <p><strong>Outdoor Sources</strong></p>
                                <ul>
                                    <li>Contaminants can also enter a building from outdoors through the outdoor air intake, or by infiltration through cracks and openings in the building envelope.</li>
                                    <li>Even outdoor air of acceptable quality can become contaminated if it is brought into the building through contaminated</li>
                                    <li>outdoor air intakes and ductwork.</li>
                                    <li>Missing or damaged screens on outdoor air intake openings can allow birds and rodents to enter these areas and soil them with nesting materials and feces.</li>

                                </ul>
