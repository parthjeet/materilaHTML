                            <td class="text-center align-middle">
                                <p><strong>Proper ventilation</strong></p>
                            </td>
                            <td>
                                <p>ASHRAE Standard offers two methods for determining the amount of outdoor air required to properly ventilate indoor occupied spaces for acceptable IAQ:</p>
                                <p><strong>1. </strong><strong>Ventilation Rate Procedure</strong></p>
                                <p>The Ventilation Rate Procedure dictates both the quantity and quality of ventilation air necessary to assure adequate dilution of contaminants generated in the occupied space. Sometimes referred to as the &ldquo;dilution solution,&rdquo; it provides a prescriptive ventilation method for achieving acceptable indoor air quality. Specifically, it prescribes a three-step approach:</p>
                                <ul>
                                    <li> First, determine the quality of the outdoor air. The designer cannot simply assume that the quality of the outdoor air surrounding the building is acceptable for use as ventilation air. Particulate or gaseous filtration is necessary if outdoor air exceeds the threshold levels referenced.</li>
                                    <li>Second, if the outdoor air is unacceptable, the standard advises that it be cleaned or filtered.</li>

                                </ul>
                                 <p><strong>2. </strong><strong>IAQ Procedure</strong></p>
                                <p>The Indoor Air Quality (IAQ) Procedure presents an alternative, performance based method for achieving acceptable indoor air quality by setting limits on the concentration of all known and specifiable contaminants. More specifically, the IAQ Procedure sets limits for 10 contaminants, prescribes a subjective analysis to determine acceptable odor levels, and describes the use of treated, recirculated air to reduce the minimum outdoor airflow rates presented in the standard.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong>Humidity management</strong></p>
                            </td>
                            <td>
                                <p>Uncontrolled moisture in buildings can contribute to unacceptable indoor air quality, occupant discomfort, and damage to the building structure and furnishings. One source of building moisture is water vapor contained in the indoor air.</p>
                                <ul>
                                    <li> ASHRAE Standard 62-2001 recommends maintaining indoor relative humidity levels between 30% and 60%.</li>
                                    <li> Humidity levels less than 30% cause some people respiratory discomfort while humidity levels over 70% near surfaces for extended periods of time promote the growth of some forms of mold and fungi.</li>
                                    <li> Today, microbiological contamination (mold and fungi) is a common cause of occupant complaints and IAQ problems in buildings.</li>
                                    <li> Although there are many different species of fungi, they all share the same basic needs for survival:
                                    <ul>
                                        <li>organic matter (nourishment)</li>
                                        <li>moisture (liquid water or relative humidity &gt; 70%)</li>
                                        <li>moderate temperatures (40&deg;F to 100&deg;F)</li>
                                        <li>source of spores</li>
                                    </ul>
                                         </li>

                                </ul>
                                <p>Controlling indoor moisture levels is one way to control propagation of fungi and dust mites inside buildings: in the carpets, wall coverings, and furnishings, as well as within the HVAC system itself.</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong>Adequate filtration</strong></p>
                            </td>
                            <td>
                                <p>Filtration is a means of controlling contaminants by reducing their concentrations to acceptable levels, or by removing them from the air stream altogether. Filtration takes one or two forms, reflecting the two general types of contaminants: particulate and gaseous. Most HVAC applications implement particulate filtration to remove particles from the air brought into and recirculated within the building. To aid designers, the standard specifically addresses how to handle outdoor air known or thought to contain contaminant concentrations that exceed safe levels.</p>
                                <p><strong>1. </strong><strong>Particulate Filtration</strong></p>
                                <p>&ldquo;Particulate contaminants&rdquo; describes a broad class of airborne physical matter that exists as discrete grains or particles. Members of this class include pollen, microorganisms, skin flakes, dust, fumes, and smoke; and their particle sizes range from 0.01 to more than 100 microns. Particles of 10 microns or less generally pose the greatest health hazard because they are small enough to penetrate the natural defenses of the body&rsquo;s respiratory system. Unfortunately, their microscopic size makes them the most difficult to remove.</p>
                                <p><strong>2. </strong><strong>Gaseous Filtration</strong></p>
                                <p>Particulates are not the only contaminants of concern in recirculated and outdoor air streams. When inside a building, concentrations of certain gases and vapors can be detrimental to occupant health. The most common gaseous indoor contaminants include carbon monoxide, radon, oxidants (ozone), nitrogen oxides (NO and NO2), and volatile organic compounds (VOCs) such as formaldehyde and toluene. Controlling VOC concentrations pose a special challenge. Filtration and source control are difficult because a building contains thousands of different VOCs of countless origins, including construction materials, furnishings, and cleaning supplies.</p>
