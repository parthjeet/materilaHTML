                    <p><strong>Good practices in construction management</strong></p>
                    <ol>
                        <li>The explosive for blasting and excavation should be stored in a standard container. Hazardous materials must not be stored near surface waters and should be stored near plastic sheeting to prevent leaks and spills. The handling of explosives should be strictly according to the guidelines as prescribed by the Department of Explosives.</li>
                        <li>Delivery of material on site must be done over a durable, impervious and level surface, so that first batch of material does not mix with the site surface. Availability of covered storage should be assured. Mobile and covered storage boxes with easy drawing and filling mechanism can be used, which can be used over a number of sites.</li>
                        <li>Demolished brick masonry and concrete is a good material for filling. Steel from RCC must be carefully segregated and rest of the material should be crushed on site only. Crushed masonry and concrete is even good for manufacture of synthetic aggregate.</li>
                        <li>The recyclable items like metal, plastic should be sent to recyclable industry, and rest of this scrap should be stored in a covered area.</li>
                        <li>Dry processes of construction are effective for reduction of water requirements and even the waste generation. Use of Interlocking bricks, pre-cast roofing and wall panels etc. will be suitable for this purpose.</li>
                        <li>Materials, which are durable and do not require frequent maintenance, should be used. Exposed brickwork in hot and dry climate with Class I bricks requires minimum maintenance. Instead of using stone masonry, stone cladding is a better way to minimize the maintenance.</li>
                        <li>Wherever materials (aggregates, sand, etc.) are more likely to generate fine airborne particles during operations, nominal wetting by water could be practiced. Workers / labour should be given proper air masks and helmets.</li>
                        <li>Skilled labour and good workmanship is must for judicial utilization of materials and minimizing the waste.</li>
                        <li>Construction is more of management. Proper estimate of material is a very first measure to minimize the undue wastage.</li>
                        <li>Contaminated runoff from storage should be captured in ditches or ponds with an oil trap at the outlet. Contaminated plastic sheeting should be packed and disposed off site.</li>
                        <li>Communities nearby the blasting site should be consulted before deciding blasting timings / durations and they should be informed / evacuated as required with the knowledge of the district collector&rsquo; office .</li>
                        <li>Bitumen emulsion should be used wherever feasible. Contractors should be encouraged to heat with kerosene, diesel or gas to gradually substitute fuel wood. Fuel wood usage for heating should be limited to unsound log i.e. dead and fallen trees.</li>
                        <li>Bitumen should not be applied during strong winds to avoid danger of forest fire. Bitumen emulsion should not be used in rains. No bitumen must be allowed to flow into the side drain. The bitumen drums should be stored in a designated place and not be scattered along the roadside</li>
                        <li>Rubbish, debris and bitumen wastes remaining after blacktop works should be cleaned and disposed off in a safe place.</li>
                        <li>Materials wasted on site should be reused at the same place. For example, use of excavated earth in landscaping, or use of waste pieces of floor tiles in floor of porch or outdoor spaces, or use of remaining pieces of glass from window panes into ventilators, skylights and boundary wall, or reuse of ply and other timber pieces into furniture etc.</li>
                    </ol>
