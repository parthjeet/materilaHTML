                    <p><strong><u>Air Pollution Control by Plants</u></strong></p>
                    <p><strong>Choosing Plants</strong></p>
                    <p>The three main criteria for selection of plants may be:</p>
                    <ul>
                        <li>Tree, shrubs should have a dense foliage with a large surface area, because leaves absorb
                            pollutants.
                        </li>
                        <li>Evergreen trees are found to be more effective.</li>
                        <li>The species chosen must be resistant to pollutants, particularly in the early stages of
                            their growth.
                        </li>
                    </ul>

                    <p><strong>The following species may be examined for their likely potential for pollution
                            control:</strong></p>
                    <ul>
                        <li>Acacia arabica (Babul)</li>
                        <li>Citrus species</li>
                        <li>Dyospyros species</li>
                        <li>Ficus bengalensis (Banyan)</li>
                        <li>Ficus religiosa (Peepal)</li>
                        <li>Lilium spp. (Lily)</li>
