                    <p><strong>SOIL EROSION CONTROL:</strong></p>
                    <p><strong>Mitigation options</strong></p>
                    <ol>
                        <li>When opening the site, care should be taken to keep vegetation clearing at a minimum.</li>
                        <li>To keep the damage to topsoil minimum, excavators must be used for construction. The
                            excavated material such as topsoil and stones should be stacked at safe places for reuse at
                            a later stage of construction.
                        </li>
                        <li>Prevent soil erosion for large sites during construction by providing sedimentation basin,
                            contour trenching, mulching, as required.
                        </li>
                    </ol>
                    <p><strong><u>There are two types of soil erosion control:</u></strong></p>
                    <ol>
                        <li><strong>Temporary controls</strong> &ndash; provide cover to the soil for a short period of
                            time, till the permanent measures are adopted. These are usually applicable during
                            construction.
                        </li>
                        <li><strong>Permanent controls</strong> &ndash; These measures are incorporated on soil, when
                            activities that disturb the soil are over. These measures could be applicable post
                            construction in the proposed landscape plan.
                        </li>
                    </ol>
                    <p>
                        <strong>1. DURING CONSTRUCTION</strong>
                    </p>
                    <div class="padding-left-2">
                        <p>During construction, there can be a lot of pollution going on, especially when it rains.
                            Pollution on construction sites can be reduced by controlling:</p>
                        <ul>
                            <li>Soil erosion</li>
                            <li>Airborne dust generation</li>
                            <li>Waterway sedimentation</li>
                        </ul>
                        <p>(Source- <a href="http://www.greenexamacademy.com/ssp1/">Green Exam Academy</a>)</p>
                        <p><strong>TEMPORARY CONTROL MEASURES</strong></p>
                        <strong>Preservation of natural vegetation and protection of landscape: </strong>The
                        <strong>preservation of natural vegetation </strong>during construction provides natural
                        buffer zones, protects soils from water and wind erosion, removes sediments and other
                        pollutants from storm water runoff, and is aesthetically pleasing.
                        <p>Planning Consideration-</p>
                        <ul>
                            <li>The preservation of vegetation should be planned before any site disturbance begins.
                            </li>
                            <li>Highly visible barricades and signs should be erected to protect vegetation boundaries
                                selected for preservation.
                            </li>
                            <li>Planning should include the maintenance requirements of the existing vegetation.</li>
                            <li>Based on soil types and climate, different species will require different maintenance
                                activities such as mowing, fertilization, irrigation, pruning, and weed/pest control.
                                These
                                activities should be performed regularly during construction.
                            </li>
                            <li>It can be used to protect unique or endangered plant species.</li>
                        </ul>
                        <p>(Source- <a href="http://envfor.nic.in/divisions/iass/Construction_Manual.pdf">Ministry of Environment and Forests,
                                Government of India</a>)</p>
                        <strong>Preservation of Air environment: </strong>Air environment of the proposed site gets
                        affected due to wind erosion, construction on site, transportation on site and heat island
                        effect.

                        <p>Planning Consideration-</p>
                        <ul>
                            <li>Principle surfaces that contribute to the heat island effect include streets, sidewalks,
                                parking lots, and buildings. Heat island effect can be minimized by use of shading or
                                reflective surfaces.
                            </li>
                        </ul>
                        <p>(Source- <a href="http://envfor.nic.in/divisions/iass/Construction_Manual.pdf">Ministry of Environment and Forests,
                                Government of India</a>)</p>
                        <p>
                            <strong>Temporary seeding</strong>: Temporary seeding means growing vegetative cover for a
                            short period of time on disturbed site areas that are prone to soil erosion. Fast growing
                            grasses are used in this system whose root systems hold the soil together so that they are
                            not carried away by storm water runoff and wind. Temporary seeding also helps in reducing
                            the problem of dust from bare soil due to construction. This method of soil stabilization
                            heavily depends upon the season and rainfall rate for success. Seeding on slopes of 2:1 or
                            more, in adverse soil conditions, regions where hot or dry season is expected or where heavy
                            rains are expected should be followed by spreading mulch.
                        </p>
                        <p>
                            <strong>Earth dikes and contour trenching:</strong> An earth dike is a ridge and channel
                            arrangement constructed parallel to the contours along the face of the slope at regular
                            intervals on the lengths and slopes greater than 10% (1:10). They are used to protect the
                            work areas from upslope runoff and to divert the sediment &ndash;laden water to sediment
                            traps. They are used for reducing runoff velocity, increasing the distance of overland
                            runoff flow, and to hold moisture and minimize sediment loading of surface runoff. The dike
                            consists of compacted soil and stone, riprap or vegetation to stabilize the channel.
                        </p>
                        <ul>
                            <li><strong>Mulching:</strong> Mulch is simply a protective layer of a material that is
                                spread
                                on the top of the soil. Mulching is a temporary soil stabilization technique. Mulches
                                can
                                either be organic, such as grass, hay, straw, wood chips, and similar materials, or
                                inorganic, such as stones and brick chips. Mulching should be used with seedlings and
                                plantings in steep slope areas (slopes&gt;33%). Steep slopes are prone to heavy erosion.
                                Netting or anchoring should be used to hold it in place. Other surface runoff control
                                measures like contour terracing to break up concentrated flows shall be installed prior
                                to
                                seeding and mulching.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Geotextiles</strong>: Geotextiles are porous fabrics which are manufactured by
                                weaving or bonding fibres made from synthetic materials. Geotextiles like nets when
                                combined
                                with mulch act as stronger mulch. Nets are made from jute or other wood fibres and can
                                be
                                used to stabilize soil while plants are growing.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Silt fence</strong>: A silt fence is a temporary measure for sedimentation
                                control.
                                This system consists of a filter fabric which is supported by posts. A stilt fence is
                                used
                                to detain sediments from a small drainage area. A silt fence should be installed prior
                                to
                                major soil disturbance in the drainage area. The fence should be placed at the bottom of
                                the
                                slope and perpendicular to the direction of flow. It could be used at the boundary end
                                of
                                construction area.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Sediment trap</strong><strong>:</strong> A sediment trap can be constructed by
                                excavating a pond across a low-lying area on the site. A temporary sediment trap could
                                be
                                used in conjunction with swales, contour trenches, earth dikes, diversion channels.
                                Sediment
                                traps are suitable for small drainage areas, less than 10 acres. The traps should be
                                maintained till the site area is permanently stabilized through vegetation and or when
                                permanent structures are in place.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Timing of construction</strong><strong>:</strong> Construction work and erosion
                                control applications shall be scheduled and sequenced during dry weather periods when
                                the
                                potential for erosion is the lowest. Slope protection techniques to control erosion
                                shall be
                                used when construction during wet season is unavoidable. Sedimentation collection
                                systems,
                                drainage systems, and runoff diversion devices shall be installed before construction
                                activity.
                            </li>
                        </ul>
                    </div>
                    <p>
                        <strong>2. POST CONSTRUCTION</strong>
                    </p>
                    <div class="padding-left-2">
                        <p>The soil conservation, sediment control and storm water management practices given below
                            shall be
                            practiced after construction is complete.</p>
                        <p>
                            <strong>Reduce Limits of Clearing &amp; Grading:</strong> Reducing the limits of clearing
                            and grading is a planning principle that can be used to preserve existing vegetation,
                            natural drainage patterns, and the aesthetics of a project site. In addition, this principle
                            helps minimize the clearing and grading costs associated with a construction project.
                        </p>
                        <p>Planning Consideration-</p>
                        <ul>
                            <li>Natural conservation areas and other site features can be protected using these
                                techniques.
                            </li>
                            <li>More undisturbed natural area on a site is preserved.</li>
                            <li>Set up limits of disturbance for development activities.</li>
                            <li>Limit the site footprint to reduce the clearing and disturbance of a site.</li>
                        </ul>
                        <p>(Source- <a href="http://envfor.nic.in/divisions/iass/Construction_Manual.pdf">Ministry of Environment and Forests,
                                Government of India</a>)</p>
                        <p>
                            <strong>Fit the Design to the Existing Terrain: </strong>Fitting the design to the existing
                            terrain allows for the planning, design, and placement of buildings, roads/streets,
                            utilities, and other infrastructure in a manner that compliments the existing topography and
                            minimizes the amount of clearing and grading of the project site.
                        </p>
                        <p>Planning Consideration-</p>
                        <ul>
                            <li>Plan and lay out roads and streets to follow natural landforms.</li>
                            <li>Position buildings and other impervious surfaces away from steep slopes, drainage ways,
                                and
                                flood plains.
                            </li>
                        </ul>
                        <p>(Source- <a href="http://envfor.nic.in/divisions/iass/Construction_Manual.pdf">Ministry of Environment and Forests,
                                Government of India</a>)</p>
                        <p><strong>Utilize Undisturbed Areas &amp; Natural Buffers:</strong> Preserving undisturbed
                            areas and using natural buffers is a planning and site design principle that can be
                            effectively used to minimize clearing and grading, filter and infiltrate storm water runoff,
                            reduce environmental impacts, and minimize the cost of development.
                        </p>
                        <p>Planning Consideration-</p>
                        <ul>
                            <li>Minimize the amount of impervious surface area and use storm water management measures
                                to
                                convert concentrated flows from the impervious areas into sheet flow that is discharged
                                into
                                pervious, vegetated areas.
                            </li>
                            <li>Use storm water management measures to convert concentrated flows into sheet flows and
                                direct the runoff towards vegetated buffer zones and undisturbed areas.
                            </li>
                            <li>Use natural depressions for storage of storm water runoff.</li>
                        </ul>
                        <p>
                            <strong>Top soil laying:</strong> Placement of topsoil or other suitable plant material over
                            disturbed lands should be carried out to provide suitable soil medium for vegetative growth.
                            Prior to spreading the topsoil, the sub-grade shall not be loosened to a depth of 50 mm to
                            permit bonding. Topsoil shall be spread uniformly at a minimum compacted depth of 50 mm on
                            grade of 1:3 or steeper slopes; a minimum depth of 100 mm on shallower slopes is essential.
                            A depth of 300 mm is preferred on relatively flatter land. If top soil is brought from
                            another site, it is important that the texture of new soil is compatible with sub soils on
                            site. Topsoil stock piled on site should be preserved from erosion through temporary
                            seeding, mulching etc.
                        </p>
                        <ul>
                            <li><strong>Permanent soil stabilization</strong><strong>:</strong> Permanent planting is
                                the
                                most effective way to prevent soil erosion, sedimentation and to stabilize disturbed and
                                undisturbed land is through the provision of vegetative cover by effective planting
                                practices. The foliage and roots of plants provide dust control and a reduction in
                                erosion
                                potential by increasing the infiltration, trapping sediment, stabilizing soil and
                                dissipating the energy of hard rain. Permanent seeding of grass and planting trees not
                                only
                                provide permanent stabilization of soil but also reduce sedimentation run. Permanent
                                seeding
                                and planting is appropriate in all those areas where land is cleared and long lived
                                plant
                                cover is desired. To establish the plants however, it is important that the trees are
                                local
                                and need low maintenance. Areas where permanent seeding and planting are particularly
                                beneficial are buffer areas, vegetated swales, steep slopes and stream banks, flood
                                plains,
                                wetlands, and other areas where erosion control would be difficult to establish, install
                                and
                                or maintain.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Structural Erosion and sedimentation control measures</strong>: Structural
                                practices
                                used in sediment and erosion control divert storm water flows away from exposed areas,
                                convey run off, prevent sediments from moving off site. These controls could be either
                                temporary or permanent measures to control erosion.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Windbreaker &amp; Shelterbelt</strong>: The term windbreaker refers to
                                protective
                                planting around gardens and orchards. Windbreakers generally consist of single or double
                                row
                                of trees. The term shelterbelt refers to extensive barrier of trees with several rows of
                                trees. Plant species are chosen with particular regard to their physical and growth
                                characteristics, and their effectiveness in achieving the desired results. Both
                                windbreakers
                                and shelterbelts have considerable visual impact in the landscape in which they are
                                situated, they therefore need to be designed so that they make a positive visual and
                                aesthetic contribution to their environment by reducing wind velocity &amp; preventing
                                soil
                                erosion. Extensive shelterbelts can also be used to augment the supply of fuel in rural
                                areas.
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Preservation of existing vegetation:</strong>
                                <ul>
                                    <li>When opening the site, care should be taken to keep vegetation clearing at a
                                        minimum.
                                    </li>
                                    <li>Vegetation cleared should be monitored and documented in terms of area, species,
                                        densities
                                        /numbers of trees etc.
                                    </li>
                                    <li>Compensatory forestation should be practiced wherever vegetation removal has
                                        been
                                        done
                                    </li>
                                    <li>Mark existing vegetation on site in surveys and follow detailed guidelines of
                                        tree
                                        preservation as per draft National building code; Part 10: Landscaping, signs,
                                        and
                                        outdoor
                                        display structures.
                                </ul>
                            </li>
                        </ul>
                        <ul>
                            <li><strong>Protection of existing vegetation</strong> (including trees, shrubs, grasses and
                                other plants) where possible, by preventing disturbance or damage to specified areas
                                during
                                construction is recommended. Preservation of natural vegetation acts a permanent control
                                measure. It minimizes erosion potential, protects water quality and provides aesthetic
                                benefits. The technique is applicable to all soil types. Areas where preservation of
                                existing vegetation are particularly beneficial are buffer areas, vegetated swales,
                                steep
                                slopes and stream banks, flood plains, wetlands, and other areas where erosion control
                                would
                                be difficult to establish, install and or maintain. This practice minimizes the amount
                                of
                                bare soil exposed to erosive forces. All existing vegetation shall be marked on a site
                                survey plan.
                            </li>
                        </ul>
                        <p>Trees retained on the project site shall be protected during the construction period by
                            following
                            measures:</p>
                        <ul>
                            <li>Damage to roots shall be prevented during trenching, placing backfill, driving or
                                parking
                                heavy equipment, dumping of trash, oil, paint, and other materials detrimental to plant
                                health by restricting these activities to outside the area of the canopy of the tree.
                            </li>
                            <li>Avoid cut and fill in the root zones, through delineating and fencing the drip line (the
                                spread limit of a canopy projected on the ground) of all the trees or group of trees.
                                Separate the zones of movement of heavy equipment, parking, or excessive foot traffic
                                from
                                the fenced plant protection zones.
                            </li>
                            <li>Trees will not be used for support; their trunks shall not be damaged by cutting and
                                carving
                                or by nailing posters, advertisements or other material.
                            </li>
                            <li>Lighting of fires or carrying out heat or gas emitting construction activity within the
                                ground, covered by canopy of the tree is not to be permitted.
                            </li>
                            <li>Young trees or saplings identified for preservation (height less than 2.00m, 0.10m trunk
                                girth at 1.00m height from finish ground, 2.00m crown diameter) within the construction
                                site
                                have to be protected using tree guards of approved specification.
                            </li>
                            <li>Existing drainage patterns through or into any preservation area shall not be modified
                                unless specifically directed by the Landscape Architect / Architect/ Engineer-in-charge.
                            </li>
                            <li>Existing grades shall be maintained around existing vegetation and lowering or raising
                                the
                                levels around the vegetation is not allowed unless specifically directed by the
                                Landscape
                                Architect /Architect / Engineer-in-charge.
                            </li>
                            <li>Maintenance activities shall be performed as needed to ensure that the vegetation
                                remains
                                healthy.
                            </li>
                            <li>The preserved vegetated area shall be inspected by the Landscape Architect / Architect /
                                Engineer-in-charge at regular intervals so that they remain undisturbed.
                            </li>
                        </ul>
                        <p>(Source- <a href="http://envfor.nic.in/divisions/iass/Construction_Manual.pdf">Ministry of Environment and Forests,
                                Government of India</a>)</p></div>
