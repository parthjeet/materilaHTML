                    <p><strong>The factors determining choice of plant materials may be thus summarized as
                            follows:</strong></p>
                    <ul>
                        <li>Environmental conditions existing on site &mdash; These include climatic, soil
                            characteristics, water table, etc.
                        </li>
                        <li>Functions which plant material has to fulfill in specific situations on a given site &mdash;
                            These may be either environmental functions (pertaining to improvement of soil conditions,
                            modification or microclimate, etc.) or design functions relating to creating spaces
                            enclosure, framing views, providing visual relief, etc.
                        </li>
                        <li>Physical characteristics and growth requirements of plant material &mdash; The former
                            include foliage density, foliage texture, leaf size and shape, flower colour, rooting
                            characteristics, etc. The latter include moisture requirements, whether the plant grows in
                            sunny or shaded conditions, etc.
                        </li>
                    </ul>
                    <p><strong>Functions of Plant Material</strong></p>
                    <ol>
                        <li><strong>Trees</strong>

                            <p class="padding-left-2">Trees perform the following functions:</p>
                            <ul>
                                <li>Protecting soil,</li>
                                <li>Modifying microclimate,</li>
                                <li>Shade,</li>
                                <li>Habitat,</li>
                                <li>Enclosure,</li>
                                <li>Direction and framing views,</li>
                                <li>Screening,</li>
                                <li>Visual relief, and</li>
                            </ul>
                        </li>

                        <li><strong>Shrubs</strong>

                            <p>The functions are similar to those of trees. Shrubs may be used together with trees to
                                reinforce
                                the functions, for example, noise barrier, shelter belts, Enclosures, etc.</p>
                            <p>Other forms in which shrubs may be used are:</p>
                            <p class="padding-left-2">
                                a) Hedges &mdash; These require regular maintenance<br>
                                b) Shrubbery &mdash; Here plants are allowed to retain their natural shape; they
                                therefore
                                require little maintenance. Shrubs provide barriers, which may either be visual or
                                physical
                                (hedges). Barriers may be required in a range of situations, for example they may be
                                only
                                for defining space, or they may be required for security and have to be, therefore,
                                necessarily impenetrable.</p>
                        </li>
                        <li><strong>Ground cover</strong>
                            <p>Ground cover plants are those which naturally grow to a very low height. Some of the uses
                                for
                                which they may be used are:</p>
                            <ol>
                                <li>Stabilizing soil on steep slopes such as embankments.</li>
                                <li>As a low maintenance substitute for grass (where the surface is not to be used).
                                </li>
                                <li>For providing variety in surface treatment.</li>
                                <li>Contrast with paving materials, for example to soften rigid lines of paving.</li>
                                <li>As a subtle means of demarcating space, as for example, in places where tall plants
                                    would be
                                    visually intrusive.
                                </li>
                                <li>In combination with other plants to provide contrast or harmony in form.</li>

                            </ol>
                        </li>
                        <li><strong> Climbers</strong>
                            <p>Certain climbers because of their spreading habits may also be used as ground cover (for
                                example
                                Asparagus spp.) Climbers are useful for shading exposed walls from direct sunlight. They
                                may
                                also be used for stabilizing soil on embankments (for example, Ficus stipulata, Ipomea
                                biloba).
                                On sites where a high degree of security makes fencing necessary, climbers and spreading
                                plants
                                like Bougainvillea species, may be trained on boundary wall.</p>
                        </li>
                    </ol>
                    <p><strong>Planting for Shelter and Soil Conservation</strong></p>
                    <p>Benefits accruing from plantation of shelter planting may be as follows:</p>
                    <ul>
                        <li>Reduction in wind velocity resulting in the arrest of movements of sand and soil
                            particles.
                        </li>
                        <li>Prevention of soil erosion.</li>
                        <li>Modification of micro-climate; moderation of change in air temperature.</li>
                        <li>Protection of crops from being blown by high winds.</li>
                        <li>Protection of livestock.</li>
                        <li>Reduction in evaporation of soil moisture. Increase in soil moisture content varies from 3
                            percent to 7.8 percent. Water loss due to evaporation is lessened.
                        </li>
                        <li>Increase in soil moisture due to greater dewfall in sheltered areas has been found to be 200
                            percent higher than on exposed ground; heaviest dew fall is over a distance of 2 to 3 times
                            the height of the shelterbelt.
                        </li>
                        <li>Beneficial effect on growth of plants that are affected by high winds.</li>
                        <li>Extensive shelterbelts may also be used to augment the supply of fuel in rural areas.</li>
                        <li>The zone of influence of shelterbelt on crop yield extends to a distance of 20 times the
                            height of the belt, with the maximum effect being observed 10 times the height of the tree
                            belt, on the leeward side.
                        </li>
                    </ul>
