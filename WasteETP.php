                    <p>
                        <strong>
                           1. Effluent Treatment Plant
                        </strong>
                        &nbsp;or&nbsp;
                        <strong>
                            ETP
                        </strong>
                        &nbsp;is one type of waste water treatment method which is particularly designed to purify industrial waste water for its reuse and its aim is to release safe water to environment from the harmful effect caused by the effluent.
                        <br />
                        <br />
                        Industrial effluents contain various materials, depending on the industry. Some effluents contain oils and grease, and some contain toxic materials (e.g., cyanide). Effluents from food and beverage factories contain degradable organic pollutants. Since industrial waste water contains a diversity of impurities and therefore specific treatment technology called ETP is required.
                        <br />
                        <br />
                        The ETP Plant works at various levels and involves various physical, chemical, biological and membrane processes to treat waste water from different industrial sectors like chemicals, drugs, pharmaceutical, refineries, dairy, ready mix plants &amp; textile etc.
                    </p>
                    <p><strong>Benefits of ETP:&nbsp;</strong></p>
                    <ol>
                        <li>To clean industry effluent and recycle it for further use</li>
                        <li>To reduce the usage of fresh water in industries</li>
                        <li>To preserve natural environment against pollution&nbsp;</li>
                        <li>To meet the standards for emission of pollutants set by the Government &amp; avoid heavy penalty</li>
                        <li>To reduce expenditure on water acquisition</li>
                    </ol>
                    <hr class="section-padding">
                    <p>
                        <strong>
                           2. Industrial Effluent Treatment Process:
                        </strong>
                        <br />
                        The treatment of different effluents varies with the type of effluent. Wastewater enters the effluent or sewage treatment plant and goes through several processes before effluent goes into the environment. Industrial effluent treatment plant process include the following stages:
                    </p>

                    <p class="img-padding-left-right">
                        <strong>
                           (a) Preliminary Treatment:&nbsp;
                        </strong>
                        Its objective is physical separation of large sized contaminants. For example cloth, paper, plastics, wood logs etc. This level/process include:
                    </p>
                    <div  class="table-responsive" >
                    <table class="table-bordered table table-striped-green text-center align-middle w-100">
                        <tbody>
                        <tr>
                            <td>
                                <p><strong>Screening</strong></p>
                            </td>
                            <td>
                                <p>This is the first unit operation that occurs in waste water treatment plants. A screen is a device with uniform openings and its purpose is to remove large floating solids.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Sedimentation</strong></p>
                            </td>
                            <td>
                                <p>&nbsp;It is a physical water treatment process using gravity to remove suspended solids from water.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Grit Chamber</strong></p>
                            </td>
                            <td>
                                <p>&nbsp;The wastewater that moves into the grit chamber removes the dense inorganic solids such as gravel, metal fragments and sand which have made their way into the sewers. Removal of grit can prevent damaging of pumps &amp; operational difficulties.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Clarifiers</strong></p>
                            </td>
                            <td>
                                <p>These are tanks built with mechanical means for continuous removal of solids being deposited by sedimentation before biological treatment.</p>
                            </td>
                        </tr>
                        </tbody>
                    </table></div>

                    <p class="img-padding-left-right">
                        <br>
                        <strong>
                           (b) Primary Treatment:
                        </strong>
                        &nbsp;Their aim is removal of floating and settles able materials such as suspended solids and organic matter. In this treatment both physical and chemical methods are used. It includes:
                    </p>
                    <div  class="table-responsive"  >
                    <table class="table table-responsive table-striped-green align-middle text-center">
                        <tbody>
                        <tr>
                            <td>
                                <p><strong>Flocculation</strong></p>
                            </td>
                            <td>
                                <p>Flocculation is a physical process and does not involve the neutralization of charge. It involves the addition of destabilized particles together into large aggregates so that they can be easily separated from the water.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Coagulation</strong></p>
                            </td>
                            <td>
                                <p>It is a process in which coagulants are added for the purpose of rapid settlement of minute solid particles in a liquid into larger mass. It permits particle removal by sedimentation and for filtration.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>&nbsp;Neutralization</strong></p>
                            </td>
                            <td>
                                <p>The main purpose of this process helps in maintaining the pH range of 6-9 to meet the requirements of different processing units in ETP system.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Primary Clarifiers</strong></p>
                            </td>
                            <td>
                                <p>These are used to slow the velocity of the water to a point where organic solids will settle to the bottom&nbsp;of the tank and it contain an equipment that is used to remove floating solids and greases from the surface.</p>
                            </td>
                        </tr>
                        </tbody>
                    </table> </div>
                    <p class="img-padding-left-right"><strong>(c) Secondary or Biological Treatment:</strong>&nbsp;The objective of this treatment is the further treatment of the effluent from primary treatment to remove the suspended solids and residual organics. In this step biological and chemical processes are involved.</p>

                    <div class="table-responsive">
                    <table class="table table-bordered table-striped-green text-center align-middle">
                        <tbody>
                        <tr>
                            <td>
                                <p><strong>Activated Sludge Process</strong></p>
                            </td>
                            <td>
                                <p>This is used for treating industrial waste water using air and a biological flock composed of bacteria.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Aerated Lagoons</strong></p>
                            </td>
                            <td>
                                <p>It is a treatment pond provided with artificial aeration to promote the biological oxidation of waste water.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Trickling Filters</strong></p>
                            </td>
                            <td>
                                <p>Trickling filters, also known as sprinkling filters, are commonly used for the biological treatment of&nbsp;domestic sewage and industrial waste water.</p>
                            </td>
                        </tr>
                        <tr>
                            <td>
                                <p><strong>Rotating Biological Contactor</strong></p>
                            </td>
                            <td>
                                <p>&nbsp;It involves allowing the wastewater to come in contact with a biological medium in order to remove pollutants in the wastewater before discharge of the treated wastewater to the environment.</p>
                            </td>
                        </tr>
                        </tbody>
                    </table></div>
                </div>
                <p class="text-justify img-padding-left-right">
                    <strong>
                       (d) Tertiary/advanced/disinfection treatment:
                    </strong>
                    The purpose of tertiary treatment is to provide a final treatment stage to raise the effluent quality to the desired level before it is reused, recycled or discharged to the environment.&nbsp;
                </p>
                <div class="table-responsive">
                <table class="table table-bordered table-striped-green text-center align-middle">
                    <tbody>
                    <tr>
                        <td>
                            <p><strong>Chemical Coagulation and sedimentation</strong></p>
                        </td>
                        <td>
                            <p>It is used to increase the removal of solids from effluent after primary and secondary treatment.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>Filtration</strong></p>
                        </td>
                        <td>
                            <p>The clarified wastewater is first passed through the adjacent filtration plant which contains large filter blocks to ensure high quality water.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>Reverse Osmosis</strong></p>
                        </td>
                        <td>
                            <p>In this process, pressure is used to force effluent through a membrane that retains contaminants on one side and allows the clean water to pass to the other side.</p>
                        </td>
                    </tr>
                    <tr>
                        <td>
                            <p><strong>&nbsp;UV Disinfection</strong></p>
                        </td>
                        <td>
                            <p>It is considered as an ideal disinfectant for industrial waste water. It leaves no residual disinfectant in the water by ensuring the water quality. It does not produce any disinfection by-products.</p>
                        </td>
                    </tr>
                    </tbody>
                </table></div>
