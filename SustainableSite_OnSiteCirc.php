                    <p><strong>Vehicular and Pedestrian Circulation</strong></p>
                    <p>The movement of people and materials into and out of a facility is determined by the design of its access, circulation, and parking systems. Such systems should be designed to maximize efficiency while minimizing conflicts between vehicle and pedestrian modes. Designers should begin with an understanding of the site&rsquo;s transportation requirements based on an analysis of how the facility will be used. This includes studying the number and types of access points that are required, the parking volume needed, pedestrian patterns, and the modes of transportation they will use. Several aspects of transportation planning can impact security and are discussed below.</p>
                    <p><strong>Roadway network design</strong></p>
                    <ul>
                        <li>Streets are generally designed to minimize travel time and maximize safety, with the end result typically being a straight path between two or more endpoints.</li>
                        <li>Although a straight line may be the most efficient course, designers should use caution when orienting streets relative to buildings.</li>
                        <li>In developing a system of street alignments with protection in mind, the designer cannot determine the size or weight of a vehicle that will travel along the road, because that is a management decision.</li>
                        <li>However, the designer can propose a roadway system to minimize vehicle velocity, thus using the roadway itself as a protective measure.</li>
                        <li>This is accomplished through the use of the following strategies.

                    <ol>
                        <li><strong>Straight-line or perpendicular approaches</strong> to buildings should not be used, because these give vehicles the opportunity to gather the speed necessary to ram through protective barriers and crash into or penetrate buildings. Instead, approaches should be parallel to the fa&ccedil;ade, with berms, high curbs, appropriate trees, or other measures used to prevent vehicles from departing the roadway. A related technique for reducing vehicle speeds is the construction of serpentine (curving) roadways with tight-radius corners.</li>
                        <li>Existing streets can be retrofitted with barriers, bollards, swing gates, or other measures to force vehicles to travel in a serpentine path. Again, high curbs and other measures should be installed to keep vehicles from departing the roadway in an effort to avoid these countermeasures.</li>
                        <li><strong>Traffic calming strategies</strong> seek to use design measures to cue drivers as to the acceptable speed for an area. These include raised crosswalks, speed humps and speed tables, pavement treatments, bulbouts, and traffic circles.</li>
                        <li>In addition to creating a more pedestrian-friendly environment, which increases &ldquo;eyes on the street&rdquo; surveillance, designing roadways to physically limit speeds can have the added benefits of increasing safety and, subsequently, lowering liability.</li>
                        <li>The distinction between speed humps, speed tables, and speed bumps is critical. Speed humps and tables are much gentler than speed bumps and can be constructed to &ldquo;enforce&rdquo; a specific speed range. Speed bumps have much more abrupt profiles and are only appropriate for low-speed applications, such as parking lots.</li>
                    </ol></li></ul>
                    <p><strong>Parking</strong></p>
                    <ul>
                        <li>Surface lots can be designed to keep vehicles away from buildings, but they consume large amounts of land and, if constructed of impervious materials, can contribute greatly to storm water runoff volume. They can also be hazardous for pedestrians if dedicated pedestrian pathways are not provided.</li>
                        <li>In contrast, on-street parking is often convenient for users and a source of revenue for local governments, but this type of parking may provide little or no setback.</li>
                        <li>Garage structures provide revenue and can be convenient for users, but they may require structural measures to ensure blast resistance as well as crime prevention measures to prevent street crime. Although the cost of land suggests that the construction of a garage below a building (either underground or aboveground) may be the most economically viable approach for many developments, they can be highly vulnerable to vehicle-borne weapons, endangering the building above.</li>
                    </ul>
                    <p>If garages must be used, human security procedures (e.g., vehicle searches) and electronic systems (e.g., closed circuit television) may be necessary to ensure safety</p>
                    <p><strong>Roads</strong></p>
