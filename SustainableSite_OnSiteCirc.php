                        <li><strong>Minimize road widths</strong></li>
                    </ol>
                    <p>Road widths can be reduced by designing roads as per average daily traffic rather than peak traffic. When roads are designed according to peak time traffic, they usually remain under-utilized most of the time. Minimizing road widths ensure reduction in use of materials and construction, and ensure minimum sire disturbance.</p>
                    <ol>
                        <li><strong>Minimize road lengths</strong></li>
                    </ol>
                    <p>Minimizing road lengths involves providing most optimal design layout on site for primary and secondary circulation. Minimizing road lengths ensure savings in material and construction, thus lesser on site disturbance. Shorter length also minimizes maintenance.</p>
