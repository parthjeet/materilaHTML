                    <p class="h4 text-center"><strong>Components of a Landfill System</strong></p>
                    <div class="table-responsive">
                        <table class="table table-bordered table-striped-green">
                        <tbody>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong>Component</strong></p>
                            </td>
                            <td class="text-center align-middle">
                                <p><strong>Material</strong></p>
                            </td>
                            <td class="text-center align-middle">
                                <p><strong>Purpose</strong></p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Landfill cover</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; Low permeable materials such as clay and geosynthetic material</p>
                                <p>&sect; A minimum thickness of 18 inches for infiltration layer and the hydraulic conductivity &lt;1 x 10<sup>-5 </sup>cm/second. An erosion layer of 6 inches&rsquo; thickness to be provided above the infiltration layer</p>
                                <p>&sect; Vegetation with the desired slope and minimal maintenance</p>
                            </td>
                            <td>
                                <p>&sect; To reduce the infiltration of water into the landfill, which in turn can reduce the leachate production</p>
                                <p>&sect; To prevent the seepage of washes, as plants can absorb water from the soil, and flow of water in the landfill</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Lining Material</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; Clay or membrane liners with a permeability of less than 7cm/second</p>
                                <p>&sect; The thickness of the membrane liners should be in the range of 0.03-0.08 inches</p>
                            </td>
                            <td>
                                <p>To prevent seepage and contamination of the groundwater</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Leachate collection</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; The collection pipes at the bottom of the liner should be at a slope of 1%</p>
                                <p>&sect; The slope of the liner should be a minimum of 2%</p>
                            </td>
                            <td>
                                <p>To facilitate the movement of the liquid into pipes</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Leachate treatment system</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; An effluent treatment plant based on aerobic or anaerobic digestion</p>
                                <p>&sect; For a low strength leachate with biological oxygen demand &lt;100 mg/liter, use an aerobic system</p>
                                <p>&sect; For a high strength leachate, use anaerobic reactors with higher efficiency</p>
                                <p>&sect; Us the system based on the end-use application and the required treated water characteristics</p>
                            </td>
                            <td>
                                <p>To treat the leachate from the landfill</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Gas collection pipes</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; The components of the gas collection system consist of interconnecting pipes; collection points, vertical extraction wells; horizontal collectors/trenches; connection to existing vents, wells flow controls; and a combustion device</p>
                            </td>
                            <td>
                                <p>&sect; To minimize the potential environmental impacts, and to recover and utilize the gas with high methane content for various applications</p>
                                <p>&sect; The permissible level of methane in soil atmosphere is 5%</p>
                                <p>&sect; To control the odor and harmful impact of the landfill gas</p>
                            </td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Vertical extraction wells</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; For a waste depth of 10m, the diameter of boreholes varies as 60-90 cm and the spacing is 60-122m</p>
                                <p>&sect; One well is needed every 0.4 hectares</p>
                                <p>&sect; Not to be used in landfills with high leachate levels</p>
                                <p>&sect; The material used is PVC or HDPE with perforations 6m below ground surface</p>
                                <p>&sect; Air infiltration is prevented by a Bentonite seal and flow control valve; a pressure monitoring port is present in the wellhead.</p>
                            </td>
                            <td>&nbsp;</td>
                        </tr>
                        <tr>
                            <td class="text-center align-middle">
                                <p><strong><em>Horizontal collector system</em></strong></p>
                            </td>
                            <td>
                                <p>&sect; Installed in trenches and covered with gravel and waste. The material can be of slotted PVC or HDPE with a size of 100mm</p>
                                <p>&sect; It can be installed at a spacing of 30-100 m</p>
                                <p>&sect; Used in landfills with elevated leachate levels</p>
                                <p>&nbsp;</p>
                            </td>
                            <td>&nbsp;</td>
                        </tr>
