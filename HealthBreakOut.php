                            <td>
                                <p><strong>Seating:</strong></p>
                            </td>
                            <td>
                                <p>Providing bar stools, a sofa or something that is dramatically different to the operator seating within the office environment provides a break in the way they sit, affecting their ability and clarity of thinking.</p>
