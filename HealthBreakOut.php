                    <p><strong>BREAKOUT SPACES</strong></p>
                    <p class="text-left">Breakout&nbsp;space design has often been thought of as just a space for staff to take a break, relax and recharge their batteries. This thinking is officially past its use-by-date! In today&rsquo;s modern workplace it&rsquo;s becoming an increasingly important area, designed to create multi-functional work areas that can be used for informal meetings as well as collaborative work.</p>


                    <p class="text-justify">The breakout space design should support spontaneous meetings &ndash; significantly reducing the demand on the traditional meeting rooms. This can reduce delays in projects and decision-making due to meeting rooms not being available when needed.</p>
                    <br>
                    <p class="text-left"><strong> Some of the key breakout space design points to help foster high levels of creativity and collaboration in your workspace include:</strong></p>
