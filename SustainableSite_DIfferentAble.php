                            <p>&middot; A parking space open on one side, allowing room for individuals in wheelchairs or individuals on braces and crutches to get in and out of an automobile onto a level surface, is adequate. It should have a minimum width of 2 700 mm preferably 2 800 mm for ambulant disabled and minimum 3 000 mm preferably 3 300 mm for wheel chair users</p>
                            <p>&middot; Parking spaces for individuals with physical disabilities when placed between two conventional diagonal or head-on parking spaces should be 3.6 m to 3.8 m wide and the length of the aisle should 7.3 m, 6.1 m and 6.5 m for head-on, 90&deg; and 60&deg; parking respectively.</p>
                            <p>&middot; Care in planning should be exercised, so that individuals in wheelchairs and individuals using braces and crutches are not compelled to wheel or walk behind parked cars.</p>
