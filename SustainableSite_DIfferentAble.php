                            <p><strong>3.12 Controls</strong></p>
                            <p>&middot; It is advantageous for wheelchair users if controls are placed at low level. For visually impaired people, they should be at eye-level.</p>
                            <p>&middot; To enable wheelchair users to reach controls while not placing them too low for visually impaired people, controls should be in the zone 900 mm to 1 200 mm from the floor. It is advantageous if controls in, for example, lifts are placed at an angle of approximately 45&deg; to the wall so that they are easier to read and operate. To cater for wheelchair users, controls should be placed not less than 400 mm from room corners. All the power and electric points should be placed at one meter above the floor level and should not project outside walls.</p>
                            <p>&middot; Again, to cater for visually impaired people, controls should be colour-contrasted with backgrounds. Information should preferably be in relief for tactile reading.</p>
                            <p>&middot; To aid operation for people with impaired co-ordination or impaired vision, switches, etc, should have large push plates.</p>
                            <p>&middot; Controls for powered door openers to hinged doors should be located so that the doors do not conflict with wheelchairs, sticks, walking aids, etc.</p>
                            <p>&middot; To facilitate operation for people with limited strength in arms and hands, handles should be easy to grip and turn.</p>
