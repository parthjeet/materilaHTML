                <p><strong>Some best practices for incorporating accessibility for persons with disabilities</strong></p>
                <ul>
                    <li>All building maintenance programs, renovations, and refurbishments to address and improvise accessibility features.</li>
                    <li>Building management policies to ensure that existing access provisions are not undermined by housekeeping practices or by untrained staff.</li>
                    <li>Periodically review the quality of accessibility by conducting user feedback surveys and access audits.</li>
                </ul>
                <p><strong>Examples of auxiliary aids/appliances</strong></p>
                <ul>
                    <li>Induction loop systems at all counters, reception desks, meeting rooms, waiting areas, and so on.</li>
                    <li>All printed information, brochures, and maps to be made available in alternative formats such as large print, audio, and Braille.</li>
                    <li>Evacuation chairs to aid in assisted evacuation of persons with impaired mobility.</li>
                    <li>Audio/tactile signage and way finding systems to aid in orientation and navigation of persons with vision impairments.</li>
                </ul>
