                        </td>
                        <td>
                            <p><strong>3.1. Ramps with Gradients</strong></p>
                            <p>&middot; Where ramps with gradients are necessary or desired, they shall conform to the following requirements (refer img 10)</p>
                            <p>&middot; A ramp when provided should not have a slope greater than 1 in 20 or maximum of 1 in 12 for short distance up to 9000 mm.</p>
                            <p>&middot; A ramp shall have handrails on at least one side, and preferably two sides, that are 900 mm high, measured from the surface of the ramp, that are smooth, and that extend 300 mm beyond the top and bottom of the ramp. Where major traffic is predominantly children, the handrails should be placed 760 mm high.</p>
                            <p>&middot; A ramp shall have a surface that is non-slip surface and if length is 3 500 mm, the minimum width shall be 1 500 mm</p>
                            <p>&middot; The provision of non-slip surfaces on ramps greatly assists the challenged persons with semi ambulatory and ambulatory disabilities. Non-slip surfaces are provided by many finishes and materials. The surfaces of the concrete ramps can be made nonskid by brooming the surface or by finishing with an indenting roller.</p>
                            <p>&middot; A ramp shall have a level platform at the top which is at least 1 800 mm long, if a door swings out onto the platform or toward the ramp. This platform shall extend at least 300 mm beyond each side of the doorway (refer img 11)</p>
                            <p>&middot; Each ramp shall have at least 1 800 mm of straight clearance at the bottom.</p>
                            <p>&middot; Ramps shall have level platforms at 10 m to 12 m intervals for purposes of rest and safety, and shall have platforms minimum 1.5 m length wherever they turn.</p>
                            <p>&middot; For visually impaired people, ramps may be colour contrasted with landing.</p>
                            <p>&middot; To minimize rise to wheelchair users, ramps should be equipped with herbs approximately 50 mm high at exposed sides.</p>
