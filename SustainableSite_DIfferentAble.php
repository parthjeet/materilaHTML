                            <p><strong>3.3. Doors &amp; Doorways</strong></p>
                            <p>&middot; <strong><em>Door width: </em></strong>To enable wheelchair users to pass through doors, the minimum clear width should be 900 mm and shall be operable by a single effort. In certain cases, the clear width should be 900 mm to 1 000 mm; for example, if the wheelchair has to be turned in the doorway, where there is a door-closer or at entrance doors to public buildings and in other situations where there is considerable traffic.</p>
                            <p>&middot; <strong><em>Side-hung doors: </em></strong>To facilitate wheelchair manoeuvre, doors should be hung with the hinges in room corners. Doors opening out into corridors or circulation spaces should be avoided as far as possible.</p>
                            <p>&middot; It is recommended that all doors have kick plates extending from the bottom of the door to at least 400 mm from the floor, or be made of a material and finish that would safely withstand the abuse they might receive from canes, crutches, wheelchair foot platforms, or wheelchair wheels.</p>
                            <p>&middot; <strong><em>Wheelchair Manoeuvring Space: </em></strong>To enable wheelchair users to approach doors manoeuvring space is needed (refer img 13). A corridor should have a width of at least 1 200 mm to allow a 90&deg; turn to be made through a door. In narrow spaces sliding doors may be preferable.</p>
                            <p>&middot; <strong><em>Thresholds: </em></strong>Raised thresholds should be avoided, but where this is not possible, their height should not exceed 25 mm. Rubber thresholds are advantageous for wheelchair users.</p>
                            <p>&middot; Care should be taken in the selection, placement and setting of door closers so that they do not prevent the use of doors by the physically disabled. Time-delay door closers are recommended.</p>
                            <p>&middot; <strong><em>Self-closing doors: </em></strong>Wheelchair users and other with impaired mobility have difficulty in using self-closing doors. The force required to open them should be reduced as far as possible. Public buildings should preferably have sliding automatic doors.</p>
                            <p>&middot; <strong><em>Door Identification: </em></strong>To help people with impaired vision to see doors, the door and frame should be in a colour which contrasts with the adjoining wall. Glass or glazed doors should be marked with a colored band or frame, a little below eye-level.</p>
                            <p>&middot; <strong><em>Handles:</em></strong> Door handles and locks should be easy to manipulate. To facilitate the closing of a door by wheelchair users (for example, a water-closet compartment), the door should have a horizontal handle approximately 800 mm from the floor. Self-closing doors should be equipped with an easy gripped vertical pull-handle with a length of at least 300 mm, and with the lower end approximately 800 mm above floor. For many people and especially those with impaired vision, it is helpful to make clear whether doors are to be pulled or pushed. (see Fig. 14)</p>
