                            <p><strong>3.14 Warning Signals</strong></p>
                            <p>&middot; Audible warning signals shall be accompanied by simultaneous visual signals for the benefit of those with hearing disabilities.</p>
                            <p>&middot; Visual signals shall be accompanied by simultaneous audible signals for the benefit of the blind. To assist blind people, lettering and symbols on signs should be in relief for tactile reading.</p>
                            <p>&middot; Signs should be designed and located so that they are easy to read. For visually impaired people, signs should preferably be at eye-level and it should be possible to approach them closely. Text and symbols should be colour-contrasted with the background. The letters should not be less than 12 mm high</p>
                            <p>&middot; Signs should be well illuminated and surfaces should not cause mirroring or reflections. Signs should not be behind glass or similar materials</p>
                            <p>&middot; Information based on colour codes only should be avoided; colorblind people may find them difficult to understand.</p>
