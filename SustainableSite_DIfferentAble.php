                            <p><strong>3.5 Stairs</strong></p>
                            <p>&middot; Stairs should not be the only means of moving between floors. They should be supplemented by lifts or ramps.</p>
                            <p>&middot; Straight flights of steps are preferred by ambulant disabled people. Treads should be approximately 300 mm deep and risers not higher than 150 mm. Steps should be of a consistent height and depth throughout the stair. Projecting nosings and open stairs should be avoided to minimize the risk of stumbling.</p>
                            <p>&middot; Handrails should be provided to both sides of any stairway. They should be continuous and extend not less than 300 mm beyond the top and bottom step (otherwise it is difficult for the disabled to use the rail at the first and last step; see Fig. 16).</p>
