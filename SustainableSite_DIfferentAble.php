                            <p><strong>3.7. </strong><strong>Sanitary Facilities</strong></p>
                            <p><strong>a. </strong>It is essential that sanitary facilities, in accordance with the nature and use of a specific building or facility, be made accessible to, and usable by, the physically challenged.</p>
                            <p><strong>b. </strong>Sanitary facilities shall have space to allow traffic of individuals in wheelchairs (see Fig. 17 and 18).</p>
