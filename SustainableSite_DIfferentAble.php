                            <p>&middot; has handrails on each side, 780 mm high and parallel to the floor, 40 mm clearance between rail and wall, and fastened securely at ends and center;</p>
                            <p>&middot; has a water-closet with the seat 500 mm from the floor.</p>
                            <p>&middot; Sanitary facilities shall have wash basins with narrow aprons, which when mounted at standard height are usable by individuals in wheelchairs; or they shall have wash basins mounted higher, when particular designs demand, so that they are usable by individuals in wheelchairs.</p>
                            <p>&middot; The drain pipes and hot-water pipes under a sanitary appliance shall be covered or insulated so that a wheelchair individual do not find it inconvenient.</p>
                            <p>&middot; Some mirrors and shelves shall be provided above the wash basins at a height as low as possible and not higher than 1 m above the floor, measured from the top of the shelf and the bottom of the mirror.</p>
                            <p>&middot; Sanitary facilities for men shall have wall mounted urinals with the opening of the basin 460 mm from the floor, or shall have floor-mounted urinals that are on level with the main floor of the toilet room.</p>
