                        <td>
                            <p><strong>Walk</strong></p>
                        </td>
                        <td>
                            <p>&middot; Public walks should be at least 1 200 mm wide and should have a gradient not greater than 1 in 20</p>
                            <p>&middot; Gradient of walks and driveways should be less than that prescribed for ramps, since walks would be devoid of handrails and kerbs and would be considerably longer and more vulnerable to the elements.</p>
                            <p>&middot; Walks or driveways should have a non-slip surface.</p>
                            <p>&middot; Such walks shall be of a continuing common surface not interrupted by steps or abrupt changes in level.</p>
                            <p>&middot; Wherever walks cross other walks, driveways, or parking lots they should blend to a common level.</p>
                            <p>&middot; The preferred method of meeting the requirement is to have the walk incline to the level of the street</p>
                            <p>&middot; at principal intersections, it is vitally important that the kerbs run parallel to the street, up to the point where the walk is inclined, at which point the kerb would turn in and gradually meet the level of the walk at its highest point. (refer img 9)</p>
                            <p>&middot; A walk shall have a level platform at the top which is at least 1 500 mm long, if a door swings out onto the platform or towards the walk. This platform shall extend at least 300 mm beyond each side of the doorway</p>
                            <p>&middot; A walk shall have a level platform at least 900 mm deep, if the door does not swing onto the platform or towards the walk. This platform shall extend at least 300 mm beyond each side of the doorway.</p>
