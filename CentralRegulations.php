                    <p><strong>1.2.1. Environmental Guidelines for Industries</strong></p>
                    <p>In order to help the concerned authorities and the entrepreneurs, it is necessary to frame certain broad guidelines for siting an industry. It is also necessary to identify the parameters that should be taken into account while setting up an industry. With this in view, the following environmental guidelines are recommended for siting of Industries to ensure optimum use of natural and man-made resources in sustainable manner with minimal depletion, degradation and/or destruction of environment. Those are in addition to those directives that are already in existence under the Industries (Development and Regulation) Act.</p>
                    <p><strong>1.2.1. Areas to be Avoided</strong></p>
