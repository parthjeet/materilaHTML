                    <p><strong>1.5. National Green Tribunal Act</strong></p>
                    <ul>
                        <li>The National Green Tribunal has been established on 18.10.2010 under the National Green Tribunal Act 2010 for effective and expeditious disposal of cases relating to environmental protection and conservation of forests and other natural resources including enforcement of any legal right relating to environment and giving relief and compensation for damages to persons and property and for matters connected therewith or incidental thereto.</li>
                        <li>It is a specialized body equipped with the necessary expertise to handle environmental disputes involving multi-disciplinary issues. The Tribunal shall not be bound by the procedure laid down under the Code of Civil Procedure, 1908, but shall be guided by principles of natural justice.</li>
                        <li>The Tribunal's dedicated jurisdiction in environmental matters shall provide speedy environmental justice and help reduce the burden of litigation in the higher courts. The Tribunal is mandated to make and endeavor for disposal of applications or appeals finally within 6 months of filing of the same.</li>
                        <li>Initially, the NGT is proposed to be set up at five places of sittings and will follow circuit procedure for making itself more accessible. New Delhi is the Principal Place of Sitting of the Tribunal and Bhopal, Pune, Kolkata and Chennai shall be the other 4 place of sitting of the Tribunal.</li>
                    </ul>
