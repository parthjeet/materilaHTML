                    <p><strong>1.2 Ministry of Environment &amp; Forests (MoEF)- EIA</strong></p>
                    <p>Environmental protection has been considered as an important domain for industrial and other developmental activities in India. Ministry of Environment &amp; Forests (MoEF) has taken several policy initiatives and promoted integration of environmental concerns in developmental projects. One such initiative is the notification on Environmental Impact Assessment (EIA) of developmental projects issued in 1994 and further revised notification in year 2006 under the provisions of Environment (Protection) Act, 1986. EIA is now mandatory for 40 categories for developmental projects. EIA Guidance Manual for building, construction, townships and area development projects proactively talks about the importance of green belts in such projects.</p>
                    <p>Environmental Guidelines for Industries developed by MoEF, suggest that the industries must care about the surrounding environment and minimize the adverse impacts of industrial operations in the immediate neighborhood as well as distant places. Therefore, these guidelines mandate project owners to maintain the certain distances by the industries from the areas like ecologically sensitive areas, Coastal areas, Flood Plain of the Riverine Systems, Transport/Communication System and Major settlements.</p>
                    <p>In addition, these guidelines also mandate that economic and social factors have to be recognized and assessed while citing industry. Following are the key points that all industries need to follow while moving ahead with the establishment of manufacturing/processing unit in certain areas. These are:</p>
