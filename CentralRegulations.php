                    <p><strong>1.2.2. Siting Criteria</strong></p>
                    <p>Economic and social factors are recognized and assessed while siting an industry. Environmental factors must be taken into consideration in industrial siting. Proximity of water sources, highway, major settlements, markets for products and raw material resources is desired for economy of production, but all the above listed systems must be away for environmental protection. Industries are, therefore, required to be sited, striking a balance between economic and environmental considerations. In such a selected site, the following factors must be recognized.</p>
                    <p><strong>1.2.3. Environmental Impact Assessment (EIA)</strong></p>
                    <p><strong>Objective: </strong>The purpose of Environmental Impact Assessment (EIA) is:</p>
                    <ul>
                        <li>To identify and evaluate the potential impacts (beneficial and adverse) of development and projects on the environmental system.</li>
                        <li>It is a useful aid for decision making based on understanding of the environment implications including social, cultural and aesthetic concerns which could be integrated with the analysis of the project costs and benefits.</li>
                        <li>This exercise should be undertaken early enough in the planning stage of projects for selection of environmentally compatible sites, process technologies and such other environmental safeguards.</li>
                    </ul>
                    <p><strong>Candidate Projects: </strong>While all industrial projects may have some environmental impacts, all of them may not be significant enough to warrant elaborate assessment procedures. The need for such exercises will have to be decided after initial evaluation of the possible implications of a particular project and its location. The projects which could be the candidates for detailed Environment Impact Assessment include the following:</p>
                    <ul>
                        <li>Those which can significantly alter the landscape, land use pattern and lead to concentration of working and service population;</li>
                        <li>Those which need upstream development activity like assured mineral and forest products supply or downstream industrial process development;</li>
                        <li>Those involving manufacture, handling and use of hazardous materials;</li>
                        <li>Those which are sited near ecologically sensitive areas, urban centers, hill resorts, places of scientific and religious importance.</li>
                        <li>Industrial Estates with constituent units of various types which could cumulatively cause significant environmental damage.</li>
                    </ul>
                    <p><strong>Factors Addressed:</strong></p>
                    <ul>
                        <li>Meteorology and air quality</li>
                    </ul>
                    <p>Ambient levels of pollutants such as Sulphur Dioxide, oxides of nitrogen, carbon monoxide, suspended particulate matters, should be determined at the center and at 3 other locations on a radius of 10 km with 120 degrees angle between stations. Additional contribution of pollutants at the locations are required to be predicted after taking into account the emission rates of the pollutants from the stacks of the proposed plant, under different meteorological conditions prevailing in the area.</p>
                    <ul>
                        <li>Hydrology and water quality</li>
                        <li>Site and its surroundings</li>
                        <li>Occupational safety and health</li>
                        <li>Details of the treatment and disposal of effluents (liquid, air and solid) and the methods of alternative uses</li>
                        <li>Transportation of raw material and details of material handling</li>
                        <li>Control equipment and measures proposed to be adopted</li>
                    </ul>
                    <p><strong>1.2.4. Environmental Management Plan (EMP)</strong></p>
                    <p>Preparation of environmental management plan is required for formulation, implementation and monitoring of environmental protection measures during and after commissioning of projects. The plans should indicate the details as to how various measures have been or are proposed to be taken including cost components as may be required. Cost of measures for environmental safeguards should be treated as an integral component of the project cost and environmental aspects should be taken into account at various stages of the projects:</p>
                    <ul>
                        <li><strong>Conceptualization:</strong> preliminary environmental assessment</li>
                        <li><strong>Planning:</strong> detailed studies of environmental impacts and design of safeguards</li>
                        <li><strong>Execution:</strong> implementation of environmental safety measures</li>
                        <li><strong>Operation:</strong> monitoring of effectiveness of built-in safeguards</li>
                    </ul>
